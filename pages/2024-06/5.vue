        sympathetic to working-class interests than previous Republican leaders.
        As in other areas, Trump’s policies did not exactly bear out the
        rhetoric. Although he shifted Republican positions on some economic
        issues—notably trade—he did not pursue economic policies that
        disproportionately benefited working-class Americans.
      </p>
      <p>
        特朗普能够利用这一趋势，常常被归因于他诉诸社会和文化怨恨，但选民也认为他在经济上没有那么保守，比之前的共和党领导人更能同情工人阶级的利益。在其他领域一样，特朗普的政策并没有兑现他的言论。虽然他在一些经济问题（尤其是贸易）上改变了共和党的立场，但他并未推行使美国工人阶级获益更多的经济政策。
      </p>
    </div>
