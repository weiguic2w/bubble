      <p>
        Given the political importance of these voters, it is not surprising
        that some on the right have called for a further shift away from the
        GOP’s traditionally conservative economic platform. Perhaps the most
        interesting and unexpected of these calls comes from the writer and
        pundit Sohrab Ahmari in Tyranny, Inc. His book is neither a policy brief
        for Trump nor a partisan attack on the Biden administration. Instead, it
        takes aim at contemporary capitalism and what Ahmari sees as the
        failures of both parties to rein in a private sector whose power has
        become a threat not only to the country’s economic well-being but to
        Americans’ freedom and liberty, as well. By getting readers to recognize
        this threat, Ahmari aims to build support for a new relationship between
        government and capitalism that would enable the former to control the
        pernicious economic and political consequences of the latter.
      </p>
      <p>
        鉴于这些选民的政治重要性，右翼中有人呼吁进一步背离共和党传统的保守经济纲领，这并不令人意外。其中最有趣、也最出人意料的呼声来自作家和评论员索拉布·阿赫玛里(Sohrab
        Ahmari)的新书《暴政公司》(Tyranny,
        Inc.)。这本书既不是为特朗普提出政策建议，也不是对拜登政府的党派攻击。而是把矛头指向了当代资本主义，以及阿赫玛里认为的两党都未能遏制私营部门的权力——这种权力不仅威胁到美国的经济福祉，还威胁到美国人的自由和权利。通过让读者认识到这一威胁，阿赫玛里的目标是为建立一种新的政府与资本主义关系争取支持，使前者能够控制后者的有害经济和政治后果。
      </p>
