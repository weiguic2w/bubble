      <h3>CORPORATE TAKEOVER</h3>
      <h3>企业收购</h3>
    </div>
    <div>
      <p>
        In many ways, Ahmari may seem a surprising figure to be making a case
        against contemporary U.S. capitalism. He began his career as an editor
        and commentator for conservative publications such as The Wall Street
        Journal, Commentary, and The New York Post. In the 2010s, he converted
        to Catholicism and evolved into a right-wing culture warrior, expressing
        sympathy for Donald Trump and Hungary’s autocratic leader Viktor Orban
        and penning jeremiads against identity politics. In 2019, he gained
        notoriety for his attack on David French, at the time a National Review
        writer and fellow conservative Christian who had argued that the culture
        wars could be fought civilly. Christians, Ahmari responded, needed to
        understand that they were involved in a real war and should fight
        accordingly, leaving unclear what that meant.
      </p>
      <p>
        在很多方面，阿赫马里的确是个奇怪的代言人，来反对当下的美国资本主义。他的职业生涯始于为《华尔街日报》、《评论》和《纽约邮报》等保守出版物担任编辑和评论员。2010年代，他皈依天主教，变成了一名右翼文化战士，对唐纳德·特朗普(Donald
        Trump)和匈牙利威权领导人维克托·欧尔班(Viktor
        Orban)表达同情，并撰文抨击身份政治。2019年，他因攻击大卫·弗伦奇(David
        French)而声名鹊起，当时后者是《国家评论》(National
        Review)的撰稿人，也是一名保守派基督徒，他认为文化战争可以文明地进行。阿赫马里反驳说，基督徒需要明白他们参与的是一场真正的战争，应该相应地去战斗——但他没有说清楚那意味着什么。
      </p>
    </div>
    <div>
      <p>
        Ahmari’s evolution took another turn in 2022, when he co-founded the
        magazine Compact with two other heterodox thinkers, one a fellow
        religious conservative, the other a dissident Marxist. Reflecting the
        diverse backgrounds of its founders, Compact describes its mission as
        promoting “a strong social-democratic state that defends community—local
        and national, familial and religious—against a libertine left and a
        libertarian right.” Now, in Tyranny, Inc., Ahmari has moved even further
        from his earlier work, setting aside cultural concerns and redirecting
        his anger toward American capitalism and its corrosive effects on
        democracy.
      </p>
      <p>
        2022年，阿赫马里与另外两位持异见的思想家——一位是宗教保守主义者，另一位是马克思主义叛逆者——共同创办了杂志《公约》(Compact)。该杂志的宗旨是支持“一个强大的社会民主国家，捍卫——无论是本地的、国家的、家庭的还是宗教的——社区，抵御自由派左翼和自由意志主义右翼”。在《暴政，公司》一书中，阿赫马里比以往任何时候都更加远离他早期的文化关怀，将他的愤怒转向美国资本主义及其对民主的腐蚀性影响。
      </p>
    </div>
    <div>
      <p>
        Although many books have criticized the economic harms of contemporary
        capitalism—rising inequality, financial insecurity, and so on—Tyranny,
        Inc. also highlights capitalism’s destructive political consequences.
        The current version of American capitalism, he argues, has generated
        vast inequalities in power that have allowed companies to coerce their
        workers, undermined choice and freedom, and turned politics into a game
        in which “one side lacks the power to play while the other side is
        structurally set up to win.”
      </p>
      <p>
        尽管已有大量著作论述了新自由主义资本主义的经济弊病——不断加剧的不平等、金融不安全感，等等——但《霸权，
        Inc.》还阐述了资本主义的政治破坏性。他认为，当前的美国资本主义形态制造了巨大的权力不平等，使得企业能够压迫员工，侵蚀选择与自由，把政治变成了一种“一方无力参与，而另一方在结构上注定会赢”的游戏。
      </p>
    </div>
    <div>
      <p>
        Take employment and workplace law. Rather than merely providing clear
        expectations about the terms and conditions of employment, many
        employment contracts now give employers sweeping control over workers,
        even beyond the workplace. Corporations may surveil employees’ web
        browsing and email and punish them for taking bathroom breaks deemed too
        long. They can even compel workers to listen to political speech. Ahmari
        cites news reports showing that, in 2019, workers at a Royal Dutch Shell
        plant in Pennsylvania were told that they would not receive overtime pay
        if they refused to attend a speech by Trump. Employers can also prevent
        employees from speaking out about abusive workplace conditions, even
        enforcing gag orders on former employees who file wrongful termination
        lawsuits.
      </p>
      <p>
        拿劳动和职场法来说。如今，许多雇佣合同不再仅仅明确雇员的工作条款和条件，还赋予雇主对员工的广泛控制权，甚至延伸到工作场所之外。企业可以监控员工的网络浏览和电邮活动，并因如厕时间过长而处罚他们。它们甚至可以强迫工人听政治演讲。阿马里提到了一则新闻报道，显示2019年，荷兰皇家壳牌在宾夕法尼亚州的一家工厂告诉工人，如果他们拒绝参加特朗普的演讲，就拿不到加班费。雇主还可以阻止员工揭露恶劣的工作环境，甚至对状告不公正解雇的前员工实施封口令。
      </p>
    </div>
    <div>
      <p>
        Alternatively, companies may require employees to use special
        arbitration courts to resolve disputes. This is an onerous process that
        can be prohibitively expensive for the employee, and its rules and
        procedures have been designed by the company for its own benefit, in
        contrast to the legal system in a democracy, in which all citizens are
        theoretically equal before the law. In one U.S. worker’s contract,
        Ahmari writes, Uber Eats required that “any dispute be resolved using
        individual, private mediation” and that arbitration proceedings would be
        held at the International Chamber of Commerce in Amsterdam. “In
        practice,” Ahmari writes, it meant that a worker “would have had to pay
        an up-front fee of $14,500 just to begin the process”—obviously daunting
        costs for an Uber driver earning approximately $2,000 a month.
      </p>
      <p>
        此外，公司还可能要求员工使用特别仲裁法庭来解决纠纷。这是一个对员工来说既费钱又费力的过程，而且其规则和程序都是公司为自身利益而设计的，有别于民主国家的法律体系，在后者的体系中，理论上所有公民在法律面前一律平等。阿赫马里写道，美国一名工人的合同规定，“任何争议都要通过单独、私密的调解来解决”，而仲裁程序则应在位于阿姆斯特丹的国际商会进行。“实际上，”他写道，“这意味着工人必须先支付14500美元的费用才能启动这一程序”——对于一个月收入约2000美元的优步司机来说，这显然是一笔令人望而却步的开销。
      </p>
    </div>
    <div>
      <p>
        Perhaps corporations’ greatest damage to democracy has been their long
        campaign to undermine labor unions. By enabling workers to confront
        employers collectively rather than individually, unions help workers
        bargain more effectively over wages, benefits, and workplace conditions,
        as well as pursue labor’s shared interests in the political arena. As
        Ahmari notes, one of the major achievements of the New Deal in the 1930s
        was the National Labor Relations Act, which gave workers the right to
        organize and soon led to a federal minimum wage, guaranteed overtime
        pay, and other regulations and policies that contributed to diminishing
        inequality and rising living standards after the Great Depression. By
        the 1950s, more than 30 percent of the U.S. labor force was unionized.
        But union membership had shrunk to just 10 percent by 2022, largely
        thanks to a concerted anti-union strategy by much of the corporate
        sector, aided and abetted by a conservative, pro-business legal movement
        and the Republican Party.
      </p>
      <p>
        也许企业对民主的最大破坏是它们长期削弱工会的运动。通过让工人集体而非个体与雇主对峙，工会有助于工人更有效地就工资、福利和工作场所条件进行谈判，并在政治领域追求劳动者的共同利益。正如阿赫马里所指出的，上世纪30年代新政的一项主要成就是《国家劳工关系法》，它赋予了工人组织起来的权利，并很快催生了联邦最低工资、保证加班费等规定和其他有助于大萧条后减少不平等现象和提高生活水平的政策。到1950年代，美国劳动力的30%多一点是组织起来的。但到2022年，由于企业部门发起的反工会战略，加上亲商业的保守派法律运动和共和党的推波助澜，工会成员比例已降至仅10%。
      </p>
    </div>
    <div>
      <p>
        Tyranny, Inc. describes the myriad tools U.S. companies use to discredit
        unions and prevent workers from building or joining them, including by
        firing employees they consider troublesome, threatening to shut down
        workplaces if workers vote for unions, and spying on employees’ efforts
        to organize. These labor-busting tactics may be deployed despite the
        progressive political leanings of the owners. Ahmari tells the story of
        a podcast that REI, the outdoor-gear chain, prepared for its workers.
        The podcast began with the company’s chief diversity officer declaring,
        “I use she/ her pronouns and am speaking to you today from the
        traditional lands of the Ohlone people,” before moving on to her main
        purpose of warning workers not to join a union.
      </p>
      <p>
        《暴政公司》描述了美国企业用来打压工会、阻止工人建立或加入工会的各种手段，包括解雇被认为难对付的员工、威胁说如果工人投票支持工会就关闭工厂，以及监视员工的组织活动。即使企业主本人的政治立场倾向于进步，这些破坏工会的策略也可能被采用。阿赫马里讲述了户外用品连锁店REI为自己的员工制作的一期播客的故事。这期播客以该公司首席多元化官的发言开头，她宣布“我使用她/她的代词，今天在奥隆人的传统领地上对你们讲话”，然后才进入正题，警告员工不要加入工会。
      </p>
    </div>
    <div>
      <p>
        Contemporary American capitalism has not only diminished the power of
        some groups while enhancing that of others, making a mockery of the
        political equality that is the foundation of any real democracy. It has
        also led to a rollback of a broad array of government regulations and
        services, which has reduced the quality of life of many citizens and
        contributed to a fraying of the country’s social fabric. For example,
        emergency services such as firefighting and ambulances have long been
        considered public goods. Now, as Ahmari observes, they are being
        outsourced, especially in underserved rural areas, to profit-seeking
        private companies, with the result that residents often pay exorbitant
        prices for deficient services. Since citizens often cannot choose
        between public and private emergency services, they risk incurring
        thousands of dollars of debt with a call to 911—presenting a wrenching
        dilemma, particularly for the poor. How are Americans, Ahmari wonders,
        supposed to see themselves as part of a common national community if
        membership in that community means less and less?
      </p>
      <p>
        当代美国资本主义不仅削弱了一些群体的权力、增强了其他群体的权力，从而嘲弄了真正民主政治的基石——政治平等。它还导致了一系列政府监管和服务的倒退，降低了众多公民的生活质量，并加剧了这个国家社会结构的瓦解。例如，消防和急救长期以来都被视为公共物品。如今，正如阿赫马里所指出的，这些服务正在被外包出去，特别是在服务不足的农村地区，交由寻求利润的私营公司运营，结果是居民经常要为低劣的服务支付高昂费用。由于公民往往无法在公共和私人应急服务之间做出选择，他们打一次911电话就可能欠下数千美元的债务——这对穷人来说尤其是一道令人痛苦的选择题。阿赫马里问道，如果成为这个国家社区的成员意味着越来越少，美国人又如何能将自己视为一个共同国家的组成部分呢？
      </p>
    </div>
    <div>
      <p>
        Ahmari argues that such abuses persist and have even increased partly
        because of another consequence of unrestrained capitalism—the emergence
        of “news deserts” in many parts of the country, principally in regions
        with poorer, less educated people where public accountability is
        especially needed. This phenomenon is not simply the outcome of
        vanishing advertising revenue and the rise of the Internet. As Ahmari
        observes, it has also been caused by cost-cutting Wall Street investors,
        who have gobbled up local newspapers and television stations with little
        interest in the longterm survival of local news. Nor do these investors
        seem to care that without these outlets, local abuses of power are much
        more likely to go unreported on and therefore unpunished.
      </p>
      <p>
        这些现象之所以持续存在甚至愈发严重，部分是因为不受约束的资本主义带来的另一个后果——美国很多地区（尤其是教育水平较低、识字率不高的地区）成为“新闻荒漠”，而这些地方尤其需要媒体进行监督。新闻业的衰落并非仅仅是广告收入减少和互联网崛起的结果。正如阿赫马里所指出的，华尔街的成本削减者也难辞其咎。他们收购了地方报纸和电视台，却对本地新闻的长期生存不感兴趣。他们似乎也不在乎如果没有这些媒体，地方性的滥权行为将更可能逍遥法外。
      </p>
    </div>
    <div>
      <p>
        Ahmari repeatedly stresses that these effects are not inevitable.
        Rather, they are the product of political choices made over many
        decades. For example, both parties have embraced neoliberal economic
        ideas and policies that have undercut the power of working Americans,
        enhanced the power of wealthy corporate elites, and weakened the ability
        of government to counter this tilt. Different political choices,
        accordingly, could reverse these problems. As Ahmari sees it, the goal
        must be to move away from a vision of capitalism in which markets have
        continually expanded at the expense of government oversight. Instead, he
        argues, the United States should aim to create a new economic order, in
        which a strong “social democratic” state keeps “markets in their proper
        place.” But what would that look like in practice?
      </p>
      <p>
        这些后果并非不可避免；它们是数十年政治抉择的结果。例如，美国两党都拥抱削弱了美国工人实力、增强了富裕企业精英的影响力、并弱化了政府抵消这种失衡能力的新自由主义经济理念和政策。因此，不同的政治抉择本可以逆转这些问题。在阿马里看来，目标必须是摆脱让市场不断侵蚀政府监管的资本主义愿景。相反，他认为，美国应该努力建立一种新的经济秩序，在其中强大的“社会民主主义”国家会让“市场处于适当的位置”。但在实践中会是什么样子呢？
      </p>
    </div>
    <div>
      <h3>CHRISTIANS INTO DEMOCRATS</h3>
      <h3>把基督徒变成民主党人</h3>
    </div>
    <div>
      <p>
        Although less familiar in the United States, the term “social democracy”
        has long been a part of the political lexicon elsewhere, especially in
        Europe, where parties bearing this name have been important political
        actors. More generally, social democracy refers to a distinct
        understanding of the relationship between capitalism and government, one
        that is based on the “primacy of politics”—that is, the idea that
        political power could and should be used to control the downsides of
        capitalism.
      </p>
      <p>
        尽管“社会民主主义”在美国不像在其他地方，特别是欧洲那样耳熟能详，但这个名字在那里的政治词汇中由来已久，而且命名的政党一直是重要的政治力量。更广泛地说，社会民主主义指的是一种关于资本主义与政府之间关系的独特理念，即“政治优先”——也就是说，可以也应该运用政治权力来遏制资本主义的负面影响。
      </p>
    </div>
    <div>
      <p>
        In contrast to their communist and Marxist counterparts, social
        democrats have historically accepted that capitalism was the best engine
        of economic growth and innovation. But unlike classical liberals, social
        democrats also feared the downsides of an unfettered market economy. In
        Scandinavia and other parts of Europe, this led them to build strong
        social safety nets, empower unions, and regulate the operation of
        markets in other ways. But social democrats also differed from the type
        of reformists and progressive liberals that have typically dominated the
        Democratic Party in the United States, except during the New Deal era,
        in which a more social democratic understanding of the economy emerged.
        Progressive U.S. liberals acknowledge that capitalism can produce such
        negative effects as economic inequality and insecurity, and that
        government needs policies to ameliorate them. But in general, these
        reformers have not been much concerned with addressing capitalism’s
        destructive political consequences, as well. Social democrats, on the
        other hand, explicitly assert that all economics is political—that the
        rules governing the economy shape political as well as economic
        outcomes, most notably the relative power of various socioeconomic
        groups.
      </p>
      <p>
        不同于共产主义者和马克思主义者，社会民主党人历来认为资本主义是推动经济增长和创新的最佳动力。但与古典自由主义者不同的是，社会民主党人也认识到不受约束的市场经济具有负面作用。在斯堪的纳维亚和其他欧洲地区，这导致他们建立了强大的社会保障网，赋予工会权力，并以其他方式监管市场运行。然而，社会民主党人与典型的改革主义者和进步自由主义者——除了新政时期，当时美国民主党中出现了一种更为社会主义的经济观念——也有所不同。美国的进步自由主义者承认，资本主义会导致经济不平等和不安全感等不良后果，需要政府政策来缓解这些问题。但总体而言，这些改革者并未过多关注解决资本主义的政治破坏性后果。相比之下，社会民主党人明确指出，所有经济都是政治——治理经济的规则会影响政治和经济结果，尤其是各个社会经济群体的相对权力。
      </p>
    </div>
    <div>
      <p>
        In recent decades in the United States, however, it is not the left but
        neoliberal conservatives who have grasped that reshaping the economic
        rules inevitably means reshaping power relationships in society. They
        have, as Ahmari puts it, engaged in “a generational effort” to weaken
        the political power of workers and obscure the reality that “private
        actors can imperil freedom just as much as overweening governments.” Yet
        social democracy involves more than a state capable of constraining
        capitalism’s negative economic and political consequences. In addition
        to the primacy of politics, social democrats have traditionally shown a
        strong commitment to liberal democracy.
      </p>
      <p>
        然而，在美国，近几十年来，左右这种力量格局发生了逆转。
        如阿赫马里所言，新自由主义保守派发动了一场“世代之战”，削弱了工人阶级的政治权力，并模糊了一个事实：“私人行为体与过度膨胀的政府一样，也会威胁自由。”然而，社会民主主义不仅仅关乎一个能够抑制资本主义负面经济和政治影响的国家。除了政治的核心作用之外，社会民主党人历来还非常重视自由民主。
      </p>
    </div>
    <div>
      <p>
        Social democrats viewed the democratic state both as the best tool for
        constraining capitalism and as the only political system consistent with
        the liberal values they held. The most important of those values is the
        ability of individuals to make their own life choices, free from
        political, social, or economic coercion. Such a commitment to individual
        freedom, liberty, and the pluralism that follows from it conflicts,
        however, with Ahmari’s previous positions. In the past, he has railed
        against those who prize “autonomy above all” and aim “to secure for the
        individual will the widest possible berth to define what is true and
        good and beautiful, against the authority of tradition.” It is the task
        of government, Ahmari has argued, to protect and promote the “common
        good” rather than maximizing private autonomy or liberty. If one assumes
        that Ahmari remains committed to some version of his earlier positions,
        then social democracy is the wrong solution for him. What other
        political traditions, then, might be compatible with his calls for a new
        economic order?
      </p>
      <p>
        在社会民主党人看来，民主国家既是遏制资本主义的最好工具，也是与他们秉持的自由价值观相一致的唯一政治制度。这些价值观中最重要的一条，就是个人能够在不受政治、社会或经济强制的情况下做出自己的人生选择。然而，这种对个人自由、权利以及随之而来的多元主义的承诺，与阿赫马蒂之前的立场相冲突。过去，他曾痛斥那些“将自主性置于一切之上”的人，他们“力图使个体意志享有尽可能大的空间来定义什么是真、善和美，对抗传统权威”。阿赫马蒂曾主张，政府的任务是保护和促进“共同利益”，而不是最大限度地保障个人自主权或自由。如果假设阿赫马蒂仍然坚持他早些时候的某些立场，那么，社会民主主义对他来说就不是解决方案。那么，还有哪些政治传统可能与他对新经济秩序的呼吁相容呢？
      </p>
    </div>
    <div>
      <p>
        Perhaps the most obvious is right-wing populism. In populist efforts to
        remake the economy, Europe may be ahead of the United States. Many
        European rightwing populist parties abandoned neoliberal and
        conservative economic policies a couple of decades ago, instead
        attacking globalization and free trade, advocating for a strong national
        state, promising to protect social welfare policies, and more generally
        claiming to champion the “left-behinds.” This reorientation has helped
        France’s National Rally, the Austrian Freedom Party, the Sweden
        Democrats, and other European rightwing populist parties become the
        largest or close to the largest working-class parties in their
        countries. These parties have not, however, paired their leftward
        economic shift with a commitment to political liberalism. And how
        committed they are to democracy remains unclear.
      </p>
      <p>
        最明显的一个例子就是右翼民粹主义。在重塑经济的民粹主义努力方面，欧洲可能已经走在了美国前面。几十年前，许多欧洲右翼民粹政党就抛弃了新自由主义和保守主义经济政策，转而攻击全球化和自由贸易，主张建立强大的民族国家，承诺保护社会福利政策，并更广泛地宣称要为“被遗忘者”代言。这一转向帮助法国国民联盟(National
        Rally)、奥地利自由党(Freedom
        Party)、瑞典民主党(Democrats)等欧洲右翼民粹政党成为各自国内最大或接近最大的工人阶级政党。然而，这些政党并未将经济上的左转与对政治自由主义的承诺结合起来。它们对民主的承诺有多大，也不清楚。
      </p>
    </div>
    <div>
      <p>
        This is certainly true of the Trumpist version of the Republican Party,
        which has made clear its disdain not merely for pluralism and individual
        rights but also for democracy itself. In his culture-warrior days,
        Ahmari certainly expressed something resembling rightwing populist
        views. But if his rejection of tyranny is principled, then embracing the
        Trumpist GOP, even if it shifts away from an embrace of neoliberal,
        free-market capitalism, is not an option since it would simply create a
        different form of tyranny.
      </p>
      <p>
        这当然适用于特朗普主义版本的共和党，该党已明确表示它不仅蔑视多元主义和个体权利，而且蔑视民主本身。在阿赫马里的文化战岁月里，他确实表达了一种类似右翼民粹主义的观点。但如果说他反对专制是出于原则，那么即便特朗普主义GOP不再拥抱新自由主义和自由市场资本主义，他也无法接受，因为它只会创造出另一种形式的专制。
      </p>
    </div>
    <div>
      <p>
        There is, however, another political tradition consistent with the kind
        of constraints on capitalism Ahmari advocates: post-1945 Christian
        democracy. Unlike right-wing populism, Christian democracy coheres with
        at least some of the traditional or religious values Ahmari championed
        earlier in his career while also maintaining a strong commitment to
        democratic institutions. Like social democracy, Christian democracy has
        not played an important role in the United States, but it has deep roots
        in Europe.
      </p>
      <p>
        然而，还有一种政治传统与阿赫马里所主张的对资本主义的约束相一致：那就是战后基督教民主主义。与右翼民粹主义不同，基督教民主主义与阿赫马里早年支持的一些传统或宗教价值观是一致的，同时也坚定地维护民主制度。如同社会民主主义一样，基督教民主主义在美国并未发挥重要作用，但它在欧洲有着深厚的根基。
      </p>
    </div>
    <div>
      <p>
        Christian democracy began in the late nineteenth century, when Catholic
        parties sprang up to protect the role of the church and religion in
        modernizing societies. They tended to be wary of capitalism, which they
        saw as threatening traditional values. Until World War II, many of these
        parties were also skeptical about liberal democracy since elections and
        majority rule might produce policies that would weaken the role of the
        church and religion in society.
      </p>
      <p>
        基督民主主义起源于19世纪末期，当时天主教政党纷纷成立，以保护教会和宗教在现代社会中的作用。这些政党往往对资本主义持谨慎态度，认为资本主义会威胁到传统价值观。直到二战之前，由于选举和多数人统治可能会产生削弱教会和宗教在社会中作用的政策，很多此类政党对自由民主也抱有怀疑态度。
      </p>
    </div>
    <div>
      <p>
        After World War II, however, the attitude of European Catholic parties
        changed. Having experienced the horrors produced by actual tyrannies,
        Catholic parties such as Germany’s Christian Democratic Union and
        Italy’s Christian Democrats committed themselves to democracy, even
        though this would entail making compromises and accepting the legitimacy
        of political actors with opposing views on the role of church and
        religion in society. Postwar Christian democratic parties also embraced
        welfare states and other restrictions on markets: in addition to being
        concerned about capitalism’s corrosive effect on traditional values,
        they now recognized the democratic benefits of market restraints. By
        providing many European countries with something they lacked before the
        war—namely, mass parties on the right that fully accepted multiparty
        democracy—the modern Christian democratic movement contributed immensely
        to Europe’s postwar stability.
      </p>
      <p>
        然而，第二次世界大战后，欧洲天主教政党的态度发生了变化。在经历了实际暴政带来的恐怖之后，德国的基督教民主联盟(cdu)和意大利的基督教民主联盟(cdu)等天主教政党致力于民主，尽管这将需要做出妥协，并接受对教会和宗教在社会中的作用持相反观点的政治人物的合法性。战后的基督教民主党派也支持福利国家和其他市场限制:除了担心资本主义对传统价值观的腐蚀作用外，他们现在认识到市场限制对民主的好处。现代基督教民主运动为许多欧洲国家提供了战前所缺乏的东西——即完全接受多党民主的群众性右翼政党——为欧洲战后的稳定做出了巨大贡献。
      </p>
    </div>
    <div>
      <p>
        In the contemporary United States, Christian movements on the right have
        until now been closer to the pre–World War II European Catholic parties
        than their postwar offshoots. Thus, echoes of the earlier position can
        be found in the Christian Nationalism and Catholic integralism
        movements, which are profoundly illiberal and prioritize the protection
        and promotion of Christian values above all else. To establish the more
        salutary approach of Europe’s Christian democrats, Ahmari would need to
        persuade religious conservatives that a better way to protect their
        interests is to work through, rather than against, the country’s
        democratic institutions.
      </p>
      <p>
        在美国，当代的基督教运动右派迄今为止更接近二战前的欧洲天主教政党，而非战后的继承者。因此，今日的基督教民族主义和天主教原教旨主义运动中回响着早先的论调，它们深深植根于反自由主义传统，将保护和促进基督教价值观置于一切之上。要采纳欧洲基督教民主党的更可取做法，阿赫马蒂需要说服宗教保守派，他们通过、而非对抗美国的民主制度来捍卫自身利益会更为有效。
      </p>
    </div>
    <div>
      <h3>AMERICA’S PARTY PROBLEM</h3>
      <h3>美国的政党问题</h3>
    </div>
    <div>
      <p>
        Tyranny, Inc. is a powerful and convincing account of the dangers that
        capitalism poses to the country’s political foundations. But saving
        American democracy requires more than taking on the private sector; it
        also requires addressing the threat posed by political parties not fully
        committed to democratic principles.
      </p>
      <p>
        《企业暴政》一书有力而令人信服地阐述了资本主义对美国政治根基构成的威胁。但拯救美国民主，仅靠对付私营部门是不够的；它还要求应对那些对民主原则并不真心实意的政党构成的挑战。
      </p>
    </div>
    <div>
      <p>
        Ahmari is correct to point out that the left’s turning away from earlier
        efforts to rein in corporate America and protect workers has contributed
        to the rise of neoliberal capitalism and hence to the dismal state of
        American democracy today. Nonetheless, the primary responsibility for
        the unhealthy state of the U.S. economy and democracy lies with the
        Republican Party. Not only have successive Republican administrations
        fought more consistently to deregulate markets, undermine the power of
        workers, and eviscerate a protective, regulatory state; they have also,
        especially since Trump, supported unprecedented attacks on democratic
        norms and institutions.
      </p>
      <p>
        阿马里正确地指出，左派抛弃早先限制美国企业权力、保护工人利益的努力，助长了新自由主义资本主义的兴起，从而导致今天美国民主的衰败。但美国经济和民主现状恶化的主要责任在于共和党。不仅历届共和党政府更坚决地推动取消市场管制、削弱工人力量、摧毁保护性和监管性国家；尤其是自特朗普时代以来，它们还支持对民主规范和制度发起空前攻击。
      </p>
    </div>
    <div>
      <p>
        Ahmari is unlikely to persuade many voters on the right to become social
        democrats, but he may be able to convince at least some of them that
        their economic and political future does not lie in a right-populist or
        Christian nationalist or integralist direction—with all the illiberalism
        and further evisceration of democracy those tendencies entail. And if he
        can direct them instead toward the profile that made Christian
        democratic parties so successful in Europe during the postwar decades—a
        defense of Christian values, a recognition that unconstrained capitalism
        is dangerous, and a principled commitment to democracy—he will be doing
        the American people a great service. Without such a reorientation of
        Trump’s GOP, however, Ahmari’s call for more government may simply
        exchange one form of tyranny for another.
      </p>
      <p>
        阿马里不大可能说服很多美国右翼选民成为社会民主党人，但他或许能说服至少一部分人认识到，他们的经济和政治前途并不在于走向右翼民粹主义、基督教民族主义或整体主义——所有这些趋势都会导致反自由主义，并进一步侵蚀民主。如果他能引导他们回归二战后几十年欧洲基督教民主党取得巨大成功的纲领——捍卫基督教价值观、认识到不受约束的资本主义的危险性，以及对民主的坚定承诺——他将为美国民众立下大功。然而，如果没有这种重新定位，特朗普的共和党即便接受阿马里关于加强政府作用的呼吁，也可能只是以一种专制形式取代另一种。
      </p>
    </div>
