      <h2>Why Bigger Isn't Always Better in Geopolitics</h2>
      <h2>地缘政治并非越大越好</h2>
    </div>
    <div>
      <p>
        The borders that carve the world into today’s states may seem indelible,
        but expand the time frame, and the lines become much more fluid. It is
        hard to find an international boundary today that has not shifted in the
        last two centuries. States are born and disappear; great powers swell,
        shrink, and vanish. In 1910, roughly 80 percent of the planet belonged
        to just a handful of European empires—and much of the rest lay in the
        possession of the Ottoman and Qing dynasties. But world wars and
        decolonization saw the rise of many new and often quite small
        nation-states. The United Nations had 51 members when it was formed in
        1945; it has 193 now. Most of these additions are old nations but new
        states that emerged from empires, including former European colonies in
        Asia and erstwhile Soviet republics. In character and scale, these
        states and the international system they compose bear little resemblance
        to the vast empires that preceded them for most of recorded history. The
        median population of the world’s countries today is 8.5 million—about
        the size of Switzerland’s.
      </p>
      <p>
        今日世界地图上的国界线看似不可更改，但只要把时间轴拉长，这些线条就会变得模糊很多。如今世界上几乎找不到一条在过去两个世纪里没有变动过的国际边界。国家诞生、消失；大国兴衰起落。1910年，地球上大约80%的陆地被少数几个欧洲帝国统治，其余部分则主要属于奥斯曼帝国和清朝。但两次世界大战以及随后的非殖民化进程导致许多新的、往往规模较小的民族国家纷纷涌现。联合国在1945年成立时有51个成员国，现在已经增加到193个。其中大多数是古老民族的新国家，它们从帝国中独立出来，包括亚洲的前欧洲殖民地和苏联加盟共和国。无论从性质还是规模来看，这些国家及其组成的国际体系与此前记录历史中的广袤帝国相去甚远。今天，全球国家的中位人口为850万，大致相当于瑞士的人口规模。
      </p>
    </div>
    <div>
      <p>
        And yet it is premature to imagine that the habits and thinking
        characteristic of that era of empires have also disappeared. Numerous
        states came into being after World War II, but their creation happened
        in parallel with another trend: the growth of what the political
        scientist Alasdair Roberts calls “superstates.” In Superstates: Empires
        of the Twenty-First Century, Roberts charts the rise of what he
        considers the biggest polities in the world today—China, India, the
        United States, and the European Union. By 2050, 40 percent of all people
        will live in those four entities. They may not be the empires of yore;
        for one thing, these superstates have a far greater responsibility for
        the welfare of their citizens than empires ever had. But they share many
        imperial challenges—namely, how to manage vast, diverse, and often
        multinational populations within the rubric of a single political
        entity. Each of these superstates addresses the task in different ways.
        But their size, economic heft, and internal complexity separate them
        from what Roberts terms “lesser states,” and these factors fundamentally
        shape geopolitics today. “The international order that is emerging in
        the twenty-first century,” Roberts writes, “is distinguished by dramatic
        differences in the scale of states.” In his view, it’s a superstate
        world, and everybody else just lives in it.
      </p>
      <p>
        然而，认为那个帝国时代的习惯和思维方式已经消失，也为时过早。二战后出现了很多国家，但它们的出现与另一个趋势并行不悖：政治学家阿拉斯代尔·罗伯茨(Alasdair
        Roberts)所说的“超国家”实体的增长。在《超国家：21世纪的帝国》(Superstates:
        Empires of the Twenty-First
        Century)一书中，罗伯茨描绘了他认为当今世界上最大的政治体——中国、印度、美国和欧盟(EU)——的崛起。到2050年，全球40%的人口将生活在这些实体之中。它们或许不是旧式的帝国；首先，这些超国家对公民福祉的责任要大得多。但它们面临着许多帝国的挑战——即如何在一个政治实体的框架内管理庞大、多民族、往往跨国的人口。这些超国家以各自不同的方式应对这一任务。但它们的规模、经济实力和内部复杂性使它们有别于罗伯茨所谓的“较小国家”，而这些因素从根本上塑造了当今的地缘政治。“正在形成中的二十一世纪国际秩序，”罗伯茨写道，“的特点是国家规模的显著差异。”在他看来，这是一个由超国家构成的世界，其他国家只是生活其中。
      </p>
    </div>
    <div>
      <p>
        This perspective may be familiar to many in Western capitals, where
        analysts and policymakers often fixate on the U.S. rivalry with China
        and the specter of great-power competition. But most people do not live
        in superstates, and they have their own worldviews and ambitions. “The
        world has never been structured to facilitate the survival of small
        states,” Armen Sarkissian notes in The Small States Club: How Small
        Smart States Can Save the World, “and treating them as disposable has
        been the norm through most of recent history.” And yet that has not
        stopped many smaller countries from thriving. In addition to being a
        professor of theoretical physics and a tech entrepreneur, Sarkissian
        served as prime minister (1996–97) and president (2018–22) of Armenia.
        He tours a succession of such countries, including Armenia, to determine
        how small states can succeed in a world that so often rewards size. With
        an emphasis on both technological and technocratic savvy, he is keen to
        show that smallness can be a boon and not a weakness on the
        international stage.
      </p>
      <p>
        对于西方国家首都的许多分析人士和政策制定者来说，这一观点可能并不陌生，他们常常关注美国与中国的竞争以及大国对抗的幽灵。但大多数人并不生活在超级国家中，他们有自己的世界观和抱负。“世界从未被设计成有利于小国生存，”阿美尼亚·萨基西安在《小国俱乐部：小而明智的国家如何拯救世界》一书中写道，“在最近的历史的大部分时间里，把它们当作一次性物品对待是常态。”但这并没有阻止许多小国繁荣发展。除了是一位理论物理学家和科技企业家，萨基西安还曾担任过亚美尼亚的总理(1996-1997年)和总统(2018-2022年)。他走访了一系列这样的国家，包括亚美尼亚，以了解小国如何在一个经常奖励规模的世界中取得成功。他强调技术和技术官僚的精明，热衷于表明，在国际舞台上，小也可以是一种优势，而不是劣势。
      </p>
    </div>
    <div>
      <p>
        Sarkissian’s book offers a corrective to the pervasive bias in favor of
        big states. For his part, Roberts reminds readers of the intrinsic
        fragility of superstates and the tremendous governance challenges they
        face. Taken together, these books reveal the complexity of an
        international system in great flux. Small states have agency in their
        dealings with the large ones, and the supposed greatness of major powers
        obscures the strains and pressures roiling within. Of course,
        superstates shape international affairs more fundamentally than smaller
        ones. But the growing great-power rivalry that threatens global peace
        and prosperity also creates the space for small and middle powers to
        build influence and thrive.
      </p>
      <p>
        萨基西安的书纠正了普遍存在的偏好大国的偏见。罗伯茨则提醒读者，超级大国本质上是脆弱的，它们面临着巨大的治理挑战。合起来看，这两本书揭示了一个正在经历巨变的复杂国际体系。小国在与大国打交道时是有主动性的，而所谓的大国伟大则掩盖了这些国家内部的紧张和压力。当然，超级大国比小国更根本地塑造着国际事务。但日益加剧的大国竞争虽然威胁到了全球和平与繁荣，但也为中小国家提升影响力和兴盛创造了空间。
      </p>
    </div>
    <div>
      <h3>TOO BIG NOT TO FAIL?</h3>
      <h3>太大而不能倒？</h3>
    </div>
    <div>
      <p>
        For much of recorded human history, empires were the dominant form of
        political organization. These states often exercised loose forms of
        control over their inhabitants and rarely mapped onto any particular
        nation—indeed, most empires ruled over diverse populations that could
        not be described by a single ethnic or linguistic identity. They were
        top-down affairs, the enterprise of an individual leader, dynastic clan,
        or ruling class, not the expression of the will of a people. As
        nation-states began to emerge in the nineteenth century, thinkers
        debated the merits of size. The German economist Friedrich List argued
        that small states would struggle to prosper and compete with imperial
        powers and other larger states. The British liberal philosopher John
        Stuart Mill, meanwhile, argued that size and diversity were the enemies
        of democracy: “Free institutions are next to impossible in a country
        made of different nationalities.”
      </p>
      <p>
        在有记录的人类历史的大部分时间里，帝国是主要的政治组织形式。这些国家往往对居民实行宽松的控制，并且很少与特定的民族重合——事实上，大多数帝国统治着无法用单一的种族或语言身份来描述的多样化人口。帝国自上而下运作，是个体领袖、王朝家族或统治阶级的事业，而非人民意志的体现。随着民族国家在19世纪开始兴起，思想家们就规模的优劣展开了辩论。德国经济学家弗里德里希·李斯特(Friedrich
        List)认为，小国将难以繁荣和与帝国强国及其他大国竞争。与此同时，英国自由主义思想家约翰·斯图亚特·密尔(John
        Stuart
        Mill)认为，规模和多样性是民主的敌人：“在由不同民族组成的国家里，几乎不可能实行自由制度。”
      </p>
    </div>
    <div>
      <p>
        Such propositions would be continually tested in the twentieth century.
        After World War I, and more definitively after World War II, a new
        international architecture emerged that oversaw the dismantling of old
        empires and the rise of a world of nation-states. Both Roberts and
        Sarkissian explain the emergence and survival of so many smaller
        countries in this period by pointing to the establishment of
        international institutions, such as the United Nations, along with the
        consolidation of norms that upheld national sovereignty and discouraged
        territorial invasion. Globalization, technological advances, and the
        lowering of commercial barriers also allowed otherwise peripheral
        countries to stake a greater position in the global economy.
      </p>
      <p>
        这些观点在20世纪受到了不断的检验。第一次世界大战后，尤其是第二次世界大战后，一个全新的国际体系出现，它见证了旧帝国的瓦解和民族国家世界的兴起。罗伯茨和萨基西安都指出，联合国等国际机构的建立，维护国家主权、反对领土侵略的规范的确立，以及全球化、技术进步和贸易壁垒的降低，都使得在这段时期出现了那么多小国，并使它们在全球经济中占据更重要的位置。
      </p>
    </div>
    <div>
      <p>
        At the same time, some big countries got bigger. The Soviet Union might
        have collapsed in 1991 (Roberts does not consider its shriveled
        successor, Russia, to be a superstate), but in quasi-imperial fashion,
        China, India, the United States, and the European Union have grown to
        rule over enormous, complex societies and territories. Roberts
        acknowledges that these modern states are different from prior empires
        in that they are beholden to their citizens. Superstates, he writes, are
        “hybrid polities, governing vast territories and diverse populations,
        and having important features of both empires and states.” They are
        similar to empires in having to hold diverse communities together over a
        vast span of territory. But they have technologies of control, such as
        the Internet and advanced surveillance tools, that empires lacked.
        Superstates must also meet welfare and human rights demands from
        subjects who are now concentrated in cities, where they can better
        organize to exert pressure on governments than could the largely
        dispersed, rural populations of previous centuries.
      </p>
      <p>
        与此同时，一些大国变得更大。苏联可能在1991年解体了（罗伯茨并不认为其缩水的继任者俄罗斯是一个超级国家），但中国、印度、美国和欧盟这四个国家却以准帝国的方式壮大起来，统治着幅员辽阔、人口众多且结构复杂的社会。罗伯茨认识到，这些现代国家与以往的帝国不同，它们有义务对其公民负责。他认为，“超级国家是混合政体，统治着广大的领土和多样的人口，兼具帝国和国家的许多特征。”它们必须像帝国一样，将不同群体团结在一个巨大的疆域内。但它们拥有帝国所不具备的控制技术，比如互联网和先进的监控工具。此外，超级国家还必须满足城市居民（他们更容易组织起来向政府施压，而以往几个世纪的农村人口则大多散居各地）提出的福利和人权诉求。
      </p>
    </div>
    <div>
      <p>
        Roberts’s principal concern is the durability of these superstates. The
        implicit assumption is that the failure or disintegration of any of
        these superstates would generate enormous instability and conflict. The
        United States is the oldest of the four, having lasted so far around 250
        years. But there is no compelling reason to think that it will survive
        another 250 years or that superstates will be more robust in general
        than the empires that preceded them. Superstates are vulnerable to a
        host of threats: external attack, rebellion, climate change, disease,
        growing internal disparities, shifts in economic competitiveness, and
        uneven technological development. Each of these issues is accentuated in
        superstates by their internal diversity and divisions over policy, since
        they lack the single-minded focus and clear policy goals of small
        states. “Never in history,” Roberts writes, “have we constructed
        polities that carry such heavy burdens.” He offers an insightful account
        of superstates’ internal governance and a thoughtful exploration of
        their similarities in diversity, fragility, leadership structures, and
        ideology.
      </p>
      <p>
        罗伯茨最关心的是这些超级国家的持久性。其潜在假设是，任何一个超级国家的失败或解体会造成巨大的不稳定和冲突。美国是最古老的超级国家，迄今为止已存在大约250年。但没有任何令人信服的理由认为它还能再存活250年，或者认为超级国家一般会比之前的帝国更强大。超级国家面临一系列威胁：外部攻击、叛乱、气候变化、疾病、日益扩大的内部差异、经济竞争力的变化以及技术发展的不均衡。所有这些问题在超级国家中都因内部的多样性和政策上的分歧而变得更加严重，因为它们缺乏小国那种一心一意的专注和明确的政策目标。“在历史上，”罗伯茨写道，“我们从未建立过承受如此重负的政治体。”他深刻地分析了超级国家的内部治理，并深入探讨了它们在多样性、脆弱性、领导结构和意识形态方面的相似之处。
      </p>
    </div>
    <div>
      <p>
        In his detailed examination of the governance of each of the four
        superstates, Roberts comes to some counterintuitive conclusions. For
        one, he sees the temptation to centralize as having the unintended
        consequence of weakening empires and superstates. The Soviet Union is a
        prime example of brittleness caused by overcentralization. By contrast,
        he sees the EU’s model of cohesion without coercion, where the union
        functions by consensus and lacks the power to enforce its decisions on
        its members, as a strength and a consistently underestimated source of
        its resilience. Roberts thus sees the EU as more durable than the other
        superstates.
      </p>
      <p>
        在对这四个超级大国的治理结构进行深入剖析后，罗伯茨得出了若干发人深省的结论。一方面，他认为强化中央集权的诱惑导致帝国和超级大国不期然地变得衰弱。苏联因过度集权而变得脆弱不堪，就是一个突出的例子。另一方面，他认为欧盟那种在没有强制的情况下实现团结的模式（欧盟通过共识运作，并无法对其成员国实施决定）是一种力量，也是其韧性的持续未被充分认识的来源。由此看来，欧盟比其他超级大国更具持久性。
      </p>
    </div>
    <div>
      <p>
        It’s not entirely clear, however, that these states are all that
        different from recent empires. The British Empire, for instance, was
        probably much more like a modern superstate than ancient Mesopotamian
        empires such as Akkad and Sumer. The United States today has a
        population not dissimilar to that of the British Empire at its height.
        The American ideal of being “a city on a hill,” Chinese leader Xi
        Jinping’s “China dream” of a world centered on Beijing, or Indian Prime
        Minister Narendra Modi’s claim to have become a Vishwa Guru (“world
        teacher”) are modern iterations of the self-important “civilizing
        missions” of earlier empires. And thanks to technology and
        globalization, borders remain porous as they did in the era of empires,
        especially for small states and even for superstates. Those forces
        continue to test the assumptions attached to the idea of the Westphalian
        state—that countries have absolute sovereignty, enforce hard borders,
        have citizens who are loyal only to them, and maintain a monopoly of
        violence. Today, the difference between empire and superstate is more a
        matter of degree than of kind. As the historian Charles Maier and others
        have argued, empires never quite vanished; they just shifted shape.
      </p>
      <p>
        然而，我们并不完全清楚，这些国家与最近的帝国有什么不同。例如，大英帝国可能更像一个现代超级大国，而不是像阿卡德和苏美尔这样的古代美索不达米亚帝国。今天美国的人口与鼎盛时期的大英帝国差不多。由于技术和全球化，边界仍然像帝国时代一样漏洞百出，尤其是对于小国，甚至是超级大国。这些力量继续考验着依附于威斯特伐利亚国家理念的假设，即国家拥有绝对主权，强制执行严格的边界，拥有只忠于他们的公民，并保持对暴力的垄断。今天，帝国和超级大国之间的区别更多的是程度上的而不是种类上的。正如历史学家查尔斯·迈尔(Charles
        Maier)等人所言，帝国从未完全消失;它们只是改变了形状。
      </p>
    </div>
    <div>
      <h3>MINNOWS IN A SHARK’S WORLD</h3>
      <h3>小鱼游进了鲨鱼的世界</h3>
    </div>
    <div>
      <p>
        Sarkissian, in stark contrast to Roberts, is interested in the travails
        of smallness, not those of bigness. His persuasive and fluent book is
        part reminiscence, part case for the significance of small states, and
        part advocacy for Armenia. It is steeped in the experiences of a man
        whose life was shaped by the disintegration of the Soviet Union. “I
        write this book from a unique vantage point,” he notes, “that of someone
        who was born and raised in a seemingly indestructible superpower and
        went on to steer the course of an apparently untenable small state.”
      </p>
      <p>
        与罗伯茨截然不同，萨尔基相关注的是小国的困境，而不是大国的挑战。他的书引人入胜，流畅易读，既带有回忆录色彩，也阐述了小国的重要性，并为亚美尼亚进行了辩护。书中充满了他个人的经历——他的生活是在苏联解体的背景下塑造的。“我从一个独特视角来写这本书，”他写道，“我是从一个看似坚不可摧的超级大国出生和长大的人，后来领导了一个看似无法维持的小国。”
      </p>
    </div>
    <div>
      <p>
        His reasonable definition of a small state is one that is “small in size
        and population (say up to or under 15 million).” In 2024, 164 of the
        world’s 237 states met those criteria, a cohort, incidentally, that
        excludes most East, South, and Southeast Asian states. Half the world’s
        states have less territory than Portugal, and there are now 41 UN member
        states that are microstates with a population of less than a million.
        (At the end of World War II, there were only two such states, Iceland
        and Luxembourg.)
      </p>
      <p>
        他对于小国的合理定义是“面积和人口都较小的国家（比如说，1500万人口以下）”。根据这一标准，2024年时，全球237个国家中有164个符合这一标准，巧合的是，大多数东亚、南亚和东南亚国家都被排除在外。如今，世界上有一半的国家领土面积不及葡萄牙，而在联合国中，人口不足百万的微型国家已有41个（而在二战结束时，只有冰岛和卢森堡两个）。
      </p>
    </div>
    <div>
      <p>
        Sarkissian argues that small states’ “survival can scarcely be taken for
        granted in an increasingly multipolar world whose order, institutions,
        and norms are being torpedoed by the velocity of political,
        geopolitical, social, and technological transformations.” The pace of
        change creates new challenges, particularly for small states. With the
        sharks of large states lunging at one another across the world, can the
        minnows do more than hide in the reefs? Sarkissian thinks so, and he
        examines the experience of ten successful small states.
      </p>
      <p>
        萨基西恩认为，在一个政治、地缘政治、社会和科技变革速度之快正在颠覆国际秩序、机构和规范、日益多极化的世界中，小国的生存“绝非易事”。变化的速度带来了新的挑战，尤其是对小国而言。在全球各大国彼此大举施压之际，小国除了躲进避风港之外，还能做些什么呢？萨基西恩认为可以，并研究了十个成功小国的经验。
      </p>
    </div>
    <div>
      <p>
        He explores the records of Botswana, Estonia, Ireland, Israel, Jordan,
        Qatar, Singapore, Switzerland, and the United Arab Emirates, ending with
        his own Armenia. Many of these countries have indeed outperformed the
        seeming impediment of their size, leveraging their locations, local
        talent, ingenuity, natural resources, and other advantages to build
        dynamic economies and often play influential roles in regional
        geopolitics. As he points out, it was the Armenian quest for
        self-determination that helped initiate the unraveling of the Soviet
        Union.
      </p>
      <p>
        他考察了博茨瓦纳、爱沙尼亚、爱尔兰、以色列、约旦、卡塔尔、新加坡、瑞士和阿联酋等国的历史，最后还有他的祖国亚美尼亚。这些国家中有许多确实克服了幅员狭小的不利条件，利用其地理位置、当地人才、创新精神、自然资源和其他优势，建立起充满活力的经济，并常常在地区地缘政治中发挥影响力。如他所指出的，正是亚美尼亚对民族自决的追求拉开了苏联解体的序幕。
      </p>
    </div>
    <div>
      <p>
        Sarkissian admires Botswana for its economic prudence and efficient
        governance, Singapore for aggressively becoming an economic force and
        diplomatic troubleshooter, and Sheikh Zayed bin Sultan al-Nahyan, the
        founder of the UAE, for wrangling the different emirates into a single
        entity. In each case, these small states set the stage for success that
        was hardly preordained or even predicted. Sarkissian finds that
        successful policy is grounded in a realistic and practical understanding
        of the international and regional situation and a visionary leader’s
        single-minded pursuit of policy goals.
      </p>
      <p>
        萨尔基相赞赏博茨瓦纳的经济谨慎和高效治理，新加坡努力成为一股经济力量和外交调停者，以及阿联酋创始人谢赫·扎耶德·本·苏丹·阿勒纳哈扬将各个酋长国捏合为一个单一实体。在所有这些情况下，这些小国为取得并非预定甚至未曾预料到的成功奠定了基础。萨尔基相发现，成功的政策基于对国际和地区形势实事求是、脚踏实地的理解，以及有远见的领导人对政策目标一心一意的追求。
      </p>
    </div>
    <div>
      <p>
        But the sample dictates the results; by choosing these states, he has
        selected cases of success, which, in truth, represent the exception
        rather than the norm among small states. These examples are surely
        instructive on their own, but the exclusion of so many other countries
        that have fared more poorly prevents the reader from learning from
        failure as well as triumph. It is unclear whether the experience of
        these exceptional states is replicable, transferable, or scalable.
      </p>
      <p>
        然而，样本决定了结果；他挑选的这些国家都是小国成功的案例，而实际上，这些案例代表的只是例外而非常态。这些案例本身当然具有启发性，但排除了许多境况更糟的其他国家，也就使读者无法从失败中汲取教训，以及从胜利中学习。不清楚这些特殊国家的经验是否可复制、可转移或可扩展。
      </p>
    </div>
    <div>
      <p>
        Sarkissian has nary a harsh word to say about all but one of these small
        states. The exception is Armenia, which he criticizes sharply for having
        squandered the opportunity of the 1994 cease-fire with Azerbaijan by
        failing to convert victory into lasting peace. That failure has produced
        disastrous results in recent years as a stronger Azerbaijan has seized
        the disputed territory of Nagorno-Karabakh and effectively expelled its
        Armenian residents. Indeed, there surely is a warning here of the risks
        of geopolitical irrelevance; Armenia’s smallness left it isolated and
        unable to fend off local adversaries and threats.
      </p>
      <p>
        萨基西安对其中大多数小国都没有什么好话，只有一个例外。他对亚美尼亚的批评尤为严厉，指责该国在1994年同阿塞拜疆达成停火协议后，未能将胜利转化为持久和平，从而白白浪费了机会。近年来，随着阿塞拜疆实力增强，这一失败导致灾难性后果：纳戈尔诺-卡拉巴赫（Nagorno-Karabakh）这个争议地区被夺回，其亚美尼亚居民也被赶走。这里无疑有一个警示：地缘政治无足轻重是有风险的；亚美尼亚的弱小使其孤立无援，无法抵御地区对手和威胁。
      </p>
    </div>
    <div>
      <p>
        For Sarkissian, all happy small states are alike. Their policies boast a
        clear national vision that steers the state. He does not mention that
        all his examples of smart small states are firmly tied to the West or
        that their closeness to the West brought economic advantages and access
        that they were clever enough to use. He lists four ingredients as
        crucial to their success and as determining their fate: a strong
        foundation of national identity, robust and savvy leadership, an
        articulated vision for their country, and methodical strategic planning.
        In addition, a coherent state structure and an internal balance of power
        with strong institutions and democracy, as in the case of Israel, can
        also help small states succeed.
      </p>
      <p>
        对萨尔基相来说，所有幸福的小国都是一样的：它们的政策都有一种清晰的国家愿景。他没有提到的是，他所列举的所有精明小国都坚定地与西方站在一起，而且它们接近西方带来了经济利益和获取利益的能力，它们聪明地利用了这些利益。他认为，这些国家成功的四个关键因素决定了它们的命运：强大的民族认同基础、强大而精明的领导层、为国家勾画出的愿景，以及系统性的战略规划。此外，像以色列那样拥有一个连贯的国家架构和内部权力平衡——包括强大而独立的制度和民主——也能帮助小国取得成功。
      </p>
    </div>
    <div>
      <p>
        Sarkissian is a techno-optimist, seeing the march of science and
        technology as enabling small states to overcome the limitations imposed
        by geography, power politics, and traditional geopolitics, thus
        disrupting the dominance of large states. As globalization has moved
        from the physical to the virtual realm, small states have found it even
        easier to reconfigure or bypass the rules. Power no longer resides
        solely in the large players; it resides in small states, too. For
        instance, Sarkissian ascribes Singapore’s success to its shift to
        research and development after 1986, culminating in the 1991 strategic
        plan that moved Singapore out of labor-intensive manufacturing to an
        economy focused on knowledge. Israel’s investment in education and
        technology has helped keep it safe from multiple enemies. The dominance
        of large states depended on their military, economic, and technological
        power. Now, small states are gaining leverage in the economic and
        technological realms using easily available communications technology
        and surveillance systems and drones in both civilian and military
        contexts. Seven of the top ten countries in the Bloomberg Innovator
        Index, which measures the quality of innovation in a particular economy,
        are small states. An eighth, the Netherlands, only just exceeds the
        population cutoff. Sarkissian sees artificial intelligence as leveling
        the playing field—creating a sort of parity between big and small states
        and making private companies such as Google and individual tycoons such
        as Elon Musk into meaningful actors.
      </p>
      <p>
        萨基西扬是个科技乐观主义者，认为科学和技术的进步让小国得以克服地理、权力政治和传统地缘政治强加的局限，从而打破大国的主导地位。随着全球化从有形世界转向无形世界，小国发现更容易重组或绕过规则。力量不再只集中在大国；小国也拥有力量。例如，萨基西扬将新加坡的成功归功于1986年后转向研发，最终在1991年制定了战略计划，将新加坡从劳动密集型制造业转变为以知识为基础的经济。以色列对教育和技术的投资有助于它在众多敌人面前保持安全。大国的主导地位曾依赖于其军事、经济和技术实力。如今，小国正在利用易于获取的通信技术和监控系统以及在民用和军用领域的无人机，在经济和技术领域获得杠杆效应。在衡量一国创新能力质量的彭博创新指数排名前十的国家中，有七个是小国。第八大国荷兰的人口刚刚超过门槛。萨基西扬认为，人工智能将拉平竞技场——在大国和小国之间创造某种均势，并使谷歌等私营公司和埃隆·马斯克等个别大亨成为重要的行为体。
      </p>
    </div>
    <div>
      <p>
        A practical politician, Sarkissian sees that the power of small states
        lies in using regional balances to ensure their survival, in building up
        their economies and militaries to deter adversaries, and in enhancing
        their attractiveness to make others work with them. Small states, he
        insists, can prosper and offer their citizens peace and stability even
        in an era of great-power competition and widening geopolitical fault
        lines.
      </p>
      <p>
        作为一名务实的政治家，萨尔基相明白小国的生存之道在于利用地区力量平衡、壮大经济和军事实力以威慑对手，以及提升自身的吸引力以让其他国家愿意与之合作。他坚信，在一个大国竞争加剧、地缘政治断层线日益显现的时代，小国也能兴旺发达，为国民提供和平与稳定。
      </p>
    </div>
    <div>
      <h3>THE PROBLEMS OF SIZE</h3>
      <h3>大小的问题</h3>
    </div>
    <div>
      <p>
        But small states, in Sarkissian’s view, aren’t just fertile ground for
        technocratic achievement. They can play an almost moral role in the
        international system. “Large states desire dominance,” he writes. “Small
        states seek stability.” He advocates a “small states club,” a body that
        would convene these countries, seek to advance their interests, and,
        invariably, be an evangelist for amity among all countries since peace
        is the condition of their survival. Such a body could help rescue the
        world from its most extreme instincts. But this assumption that small
        states always act rationally and pursue the greater good is belied by
        the fact that more small states have failed than succeeded in building
        themselves economically and militarily. Sarkissian is guilty of a touch
        of romanticism in his thinking about small states; these countries are
        hardly immune to internal strife or averse to making war, and they often
        draw great powers into their quarrels.
      </p>
      <p>
        但在萨尔基相看来，小国的意义不仅如此。
        在国际体系中，它们还可以发挥某种道德作用。“大国追求支配地位，”他写道，“小国寻求稳定。”他呼吁成立一个“小国俱乐部”，让这些国家聚在一起，推动它们的利益，并不可避免地成为各国和睦相处的传教士——因为和平是它们生存的条件。这样的机构可以帮助世界摆脱最糟糕的本能。但认为小国总是理性行事、追求公益的看法未免过于乐观。事实上，在经济和军事建设方面取得成功的国家中，失败的小国多于成功的小国。萨尔基相对小国的思考带有一丝浪漫主义色彩：这些国家并非不受内讧之苦，也不排斥发动战争，而且它们的冲突往往会把大国卷入其中。
      </p>
    </div>
    <div>
      <p>
        Yet both Sarkissian and Roberts seem to understand the emergence and
        survival of small states as evidence of a less bloody and more ordered
        world. After 1950, attitudes toward the legitimacy of war changed
        radically and found expression in international law and norms. This
        belief may have been popular in the West, and it is an offshoot of the
        common Western conceit that the Cold War was largely peaceful. But it
        ignores the truth that in its killing fields, which were largely in
        maritime Asia, 1,200 people were killed every day of the Cold War.
        Whether or not one agrees with the historian Charles Tilly that “war
        made the state, and the state made war,” it could be argued that the
        creation of small states served the Cold War needs of great powers and
        superpowers. The United States and the Soviet Union assisted
        decolonization and the breakup of the older European empires in the
        1950s and 1960s at least in part because it enabled them to find clients
        and to continue controlling international affairs while waging war where
        it suited them, away from their homelands.
      </p>
      <p>
        然而，萨尔基相和罗伯茨似乎都明白，小国的出现和生存是世界变得不那么血腥、更有秩序的证据。1950年以后，人们对战争合法性的看法发生了根本变化，这在国际法律和规范中得到了体现。这一信念在西方或许颇受欢迎；它也是西方人普遍认为冷战大体上是和平的这一自满情绪的产物。但这种观点忽视了一个事实：在主要位于亚洲海域的冷战战场上，每天有1200人丧生。无论人们是否同意历史学家查尔斯·蒂利(Charles
        Tilly)的论断——“国家制造了战争，战争也制造了国家”——可以说，小国的创建至少部分地服务于超级大国在冷战期间的需求。美国和苏联在1950年代和1960年代支持非殖民化和旧欧洲帝国的瓦解，部分原因在于，这使它们能够找到代理人，并在远离本土的地方进行战争，从而继续控制国际事务。
      </p>
    </div>
    <div>
      <p>
        Today, great-power rivalry in a world between orders has altered the
        context in which big and small states operate. Great-power competition
        offers smaller states the chance to hedge and play bigger states off
        against one another. But the turn away from globalization, on which so
        many small states depend, could have damaging consequences. All states
        are affected by the lack of a settled international order, the resulting
        ineffectiveness of the multilateral system, and the weakening of
        post–World War II norms, but these trends hit small states hardest.
        Indeed, the current inchoate order has seen—contrary to the authors’
        view—an increasing reliance on force and the militarization of the
        foreign policy of larger states in the international system.
      </p>
      <p>
        在旧秩序与新秩序之间的当今世界，大国竞争改变了大小国家运作的环境。大国之间的竞争为小国提供了对冲和利用大国相互制衡的机会。但背离全球化——许多小国都依赖全球化——可能会产生破坏性后果。所有国家都受到国际秩序不明朗、多边体系因此而效率低下以及二战后规范遭到侵蚀的影响，但这些趋势对小国的打击尤为沉重。事实上，与两位作者的看法相反，当前这种未成形的秩序导致大国更加依赖武力，其外交政策军事化倾向增强。
      </p>
    </div>
    <div>
      <p>
        If big states suffer problems of cohesion, small states suffer the
        consequences of weakness. The recent record shows that large states can
        often be frustrated in imposing their will on smaller ones—see Russian
        experiences in Ukraine and the U.S. record in Afghanistan. Equally,
        small states, such as Armenia, can struggle because of their smallness,
        losing territory to more powerful adversaries. But irrespective of size,
        today’s global disorder affects all states, as both big and small find
        it harder to create the outcomes they desire.
      </p>
      <p>
        大国若内部不团结，小国就会显得力量不足。近期的历史表明，大国往往难以迫使小国屈服——看看俄罗斯在乌克兰的遭遇以及美国在阿富汗的记录就知道了。同样，小国，比如亚美尼亚，也会因其弱小而举步维艰，任由更强大的对手夺走领土。但不论大小，当今世界的动荡局势都影响着所有国家，因为它们发现越来越难以按自己的意愿塑造结果。
      </p>
    </div>
