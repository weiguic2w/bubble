        the Liberation Tigers of Tamil Eelam by trapping the group on a small
        strip of land in the northeastern region of the island country. The
        resulting operation killed tens of thousands of civilians, according to
        the United Nations. But it also eliminated the LTTE leadership,
        effectively ending the group and the broader civil war that had raged in
        Sri Lanka for nearly three decades.
      </p>
      <p>
