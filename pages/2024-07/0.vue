        A second way a terrorist group can end is by transforming into something
        else: a criminal network or an insurgency. Criminality and terrorism
        overlap, so that particular shift is more like moving along a spectrum
        than like morphing into something new as a group stops trying to
        catalyze political change in favor of exploiting the status quo for
        monetary gain. A shift to insurgency happens when a group mobilizes
        enough of the population that it can challenge the state for control of
        territory and resources. That, unfortunately, is a possible outcome in
        Gaza—and perhaps the West Bank and even Israel proper—if Israel
        maintains its current strategy.
      </p>
