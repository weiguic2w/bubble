      <p>
        A third way terrorist groups end is through successful military
        repression on the part of a state. That is the ending that Israel’s
        current campaign against Hamas hopes to bring about. Repression can
        succeed, although at enormous costs. Take, for example, Russia’s second
        campaign against separatists in Chechnya, which began in 1999 and
        continued for nearly a decade. Accurate figures are hard to come by,
        since Russian authorities prevented journalists from reporting on the
        conflict (and even targeted some who tried), but most independent
        sources have estimated that at least 25,000 civilians were killed and
        that hundreds of thousands were displaced. The bloodshed was massive and
        the destruction epic, but Russia did wipe out the main separatist
        groups, depopulating the region and paving the way for a pro-Russian
        government.
      </p>
