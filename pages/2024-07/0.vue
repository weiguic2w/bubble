        The least common pathway to termination is success; a small number of
        groups cease to exist because they achieve their goals. One familiar
        example is uMkhonto weSizwe, the military wing of the African National
        Congress in South Africa, which carried out attacks on civilians early
        in its campaign to end apartheid. Another is the Irgun, the Jewish
        militant group that employed terrorism in an effort to push the British
        out of Palestine, force many Arab communities to flee, and help lay the
        groundwork for the establishment of Israel.
      </p>
      <p>
        最不常见的终结方式是成功；只有少数团体因为实现目标而消亡。一个人们熟悉的例子是南非非洲人国民大会的军事分支“
