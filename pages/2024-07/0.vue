      </p>
    </div>
    <div>
      <p>
        Overall, however, military repression has a poor track record as a form
        of counterterrorism. It is difficult and costly to sustain and tends to
        work best when members of a terrorist group can be separated from the
        general population, a condition that is hard to create in most places.
        Repressive campaigns erode civil liberties and strain the fabric of the
        state. Scorched-earth tactics change the character of society and raise
        the question of what, precisely, the government is defending.
      </p>
      <p>
        然而，总体而言，军事镇压并不是一种有效的反恐手段。军事镇压难以持久，成本高昂，而且往往只有在恐怖分子能够与平民区分开来时才最为有效——而这在大多数情况下都很难做到。镇压运动会侵蚀公民自由，损害国家结构。焦土战术会改变一个社会的性质，并引发这样的疑问：政府到底在捍卫什么？
      </p>
    </div>
    <div>
      <p>
        Consider, for example, Uruguay in the early 1960s. At the time, the
        country had a robust party system, an educated urban population, and an
        established liberal democratic tradition. But when the Tupamaros, a
        Marxist-Leninist group, carried out a series of assassinations, bank
        robberies, and kidnappings, the government unleashed the armed forces.
        By 1972, the military had eradicated the group. Even though the attacks
        had ended, the army then launched a coup, suspended the constitution,
        dissolved parliament, and established a military dictatorship that ruled
        the country until 1985. In their short campaign, the Tupamaros had
        carried out 13 bombings (with an unknown number of casualties), executed
        one hostage, and assassinated fewer than ten officials. The military
        regime, however, killed, maimed, or displaced thousands. The Tupamaros
        were gone, but ordinary Uruguayans remained the victims of violence,
        only now at the hands of the state, as the military government destroyed
        the country’s democracy.
      </p>
      <p>
        以1960年代初期的乌拉圭为例。当时，这个国家有一个稳健的政党体系、受过教育的城市人口以及成熟的自由民主传统。然而，当马列主义组织图帕马洛斯实施了一系列暗杀、银行抢劫和绑架事件后，政府动用了军队。到1972年，军方消灭了该组织。尽管袭击已经停止，但军队随后发动政变，暂停宪法，解散议会，建立了军事独裁政权，一直统治该国直到1985年。在短暂的运动中，图帕马洛斯实施了13起爆炸（造成不明数量的伤亡），处决了一名人质，并杀害了不到十名官员。然而，军政权杀害、致残或流放了数千人。图帕马洛斯或许消失了，但乌拉圭人仍然生活在暴力之中，只是现在暴力来自国家，因为军政府摧毁了该国的民主制度。
      </p>
    </div>
    <div>
      <p>
        In explaining their repressive approach in Gaza, Israeli leaders have
        argued that Hamas is similar to the Islamic State (also known as ISIS)
        and can be defeated in a similar way. It is true that, by 2017, a
        U.S.-led coalition had reconquered territory that ISIS seized in Iraq
        and Syria in 2014, reducing the group’s presence in those places. Yet
        ISIS has not ended. Instead, it has splintered into nine groups it calls
        “provinces,” which are based all over the world and still plot and
        sometimes successfully carry out bloody attacks. This past March,
        ISIS-K—the group’s “Khorasan province,” based in Afghanistan—attacked a
        concert hall near Moscow, killing more than 140 people. Moreover, unlike
        ISIS, which is an explicitly transnational movement, Hamas is an
        exclusively Palestinian group, focused on winning control of contested
        territory. Military force can degrade Hamas’s hold on Gaza, but without
        a political solution to the underlying territorial dispute, the group
        would soon reemerge in some form and resume targeting Israeli military
        forces and civilians.
      </p>
      <p>
        以军方将领为首的一些以色列领导人辩解说，哈马斯与伊斯兰国(ISIS)并无二致，因此可以以相似的方式加以铲除。诚然，在美国领导的联军于2017年收复了ISIS在2014年占领的伊拉克和叙利亚领土后，该组织在这些地方的存在已大大减少。然而，ISIS并未终结。相反，它裂变出九个自称“省”的团体，散布世界各地，仍在策划并时不时成功实施血腥袭击。今年3月，ISIS的“呼罗珊省”(ISIS-K)在莫斯科附近制造了一起音乐会恐怖袭击，造成140多人死亡。此外，与这个明确追求跨国目标的ISIS不同，哈马斯是一个彻头彻尾的巴勒斯坦团体，专注于夺取对争议领土的控制。武力或许能削弱哈马斯对加沙的控制，但如果不解决领土争端的政治问题，哈马斯就会以某种形式死灰复燃，重新把矛头对准以色列军队和民众。
      </p>
    </div>
    <div>
      <p>
        Some might argue that the real trouble is not that Israel is relying on
        the wrong strategy but that it doesn’t have the right target. In this
        view, it is Iran, and not Hamas, that is the heart of the problem, since
        the theocratic regime in Tehran supports, arms, and funds the terrorist
        group. But any government that launches an attack against the state
        sponsor of a terrorist group risks getting itself into an even bigger
        mess. This past April, Israel and Iran engaged in an unprecedented
        series of tit-for-tat attacks that could have escalated into a
        full-blown war. But both countries eventually stepped back from the
        brink, and for now, Israel remains rightly focused on dealing with Hamas
        directly.
      </p>
      <p>
        一种观点认为，问题不在于以色列的战略错误，而在于它找错了目标。在伊朗看来，哈马斯并不是问题的核心，德黑兰的神权政体才是，因为它支持、武装并资助这个恐怖组织。但是，任何对恐怖组织的赞助国发动袭击的政府都有可能使自己陷入更大的困境。今年4月，以色列和伊朗发生了一系列前所未有的针锋相对的袭击事件，差点演变成全面战争。但两国最终都悬崖勒马，目前，以色列仍然正确地专注于直接对付哈马斯。
      </p>
    </div>
    <div>
      <p>
        Ultimately, Israel’s lack of success in Gaza so far should come as no
        surprise: counterterrorism that is purely military rarely works and is
        especially difficult for a democracy to pull off. For one thing, it
        requires suppressing media coverage to a degree that is difficult to
        achieve in today’s global digital media landscape (although the
        Committee to Protect Journalists reports that more than 100 journalists
        and media workers have been killed in Gaza since the war began). Also,
        compared with other governments that have relied on military repression
        in fighting terrorists, many of which are authoritarian, Israel is
        somewhat more hemmed in by its own laws and policies and because it
        relies heavily on a patron—the United States—that criticizes the use of
        excessive force, opposes the commission of war crimes, and at least
        putatively conditions its military aid on lawful conduct.
      </p>
      <p>
        因此，迄今为止以色列在加沙的行动未能取得成功也就不足为奇了：纯军事手段的反恐很少奏效，对一个民主国家来说尤其如此。首先，这需要压制媒体报道，而在当今全球化的数字媒体环境下，这很难做到(尽管保护记者委员会报道说，自战争开始以来，已有100多名记者和媒体工作人员在加沙丧生)。此外，与依靠军事镇压打击恐怖主义的其他国家(其中许多是专制政权)相比，以色列在更大程度上受到本国法律和政策的约束，而且它严重依赖于美国这个赞助者，而美国批评过度使用武力，反对犯下战争罪，并至少名义上将法律行为作为提供军事援助的条件。
      </p>
    </div>
    <div>
      <h3>OFF WITH THEIR HEADS</h3>
      <h3>砍掉他们的头！</h3>
    </div>
    <div>
      <p>
        A fourth way that terrorist groups end is through decapitation: the
        arrest or killing of leaders. Direct Action, a radical left-wing French
        group, carried out a campaign of assassinations and bombings in the
        1980s but ceased operations after the arrest of its principal leaders in
        1987. In 1992, Abimael Guzmán, the leader of the far-left Peruvian
        terrorist militia the Shining Path, was arrested; violence immediately
        declined, the militants accepted a government amnesty, and the group
        fragmented into much smaller narco-criminal gangs over the next ten
        years. Aum Shinrikyo, a Japanese terrorist doomsday cult, changed its
        name and eventually renounced violence after its leader, Shoko Asahara,
        was arrested in 1995.
      </p>
      <p>
        恐怖组织终结的第四种方式是“斩首”——逮捕或击毙领导人。法国极左翼组织“直接行动”在1980年代实施了一系列暗杀和爆炸活动，但在其主要领导人于1987年被捕后停止了行动。1992年，秘鲁极左翼恐怖准军事组织“光辉道路”的领导人阿布迈尔·古斯曼被捕，暴力行为随即减少，武装分子接受了政府的赦免，该组织在接下来的十年里分裂成多个规模小得多的贩毒犯罪团伙。“奥姆真理教”在1995年其领导人麻原彰晃被捕后改名，并最终放弃了暴力。
      </p>
    </div>
    <div>
      <p>
        Groups that end through decapitation tend to be small, hierarchically
        structured, and characterized by a cult of personality, and they usually
        lack a viable succession plan. On average, they have been operating for
        less than ten years. Older, highly networked groups can reorganize and
        survive.
      </p>
      <p>
        那些因高层被击杀而覆灭的组织往往规模小、等级森严、具有个人崇拜色彩，而且通常没有可行的接班计划。它们平均而言成立不到十年。而那些历史悠久、成员之间联系紧密的组织能够重组并存活下来。
      </p>
    </div>
    <div>
      <p>
        Hamas, then, is not a good candidate for a decapitation strategy. It is
        a highly networked organization that is almost 40 years old. If killing
        Hamas leaders could end the group, it would have happened long ago—and
        the Israelis have certainly tried. In 1996, Israeli security forces set
        off an explosive device inside a mobile phone used by Yahya Ayyash, a
        senior figure in Hamas and the group’s chief bomb maker; he died
        instantly. With the outbreak of the second intifada a few years later,
        the assassinations ramped up, and in 2004, Israel killed Hamas’s
        founder, Ahmed Yassin.
      </p>
      <p>
        因此，哈马斯并不是斩首行动的理想目标。这是一个有着将近40年历史的高度网络化的组织。如果刺杀哈马斯领导人就能消灭这个组织的话，那么这一幕早就发生了——以色列人肯定也试过。1996年，以色列安全部队在哈马斯高级官员、该组织首席炸弹制造者亚亚·阿亚什经常使用的手机中安装了爆炸装置，阿亚什当场死亡。几年后第二次巴勒斯坦起义爆发，暗杀行动升级，2004年，以色列杀害了哈马斯创始人艾哈迈德·亚辛。
      </p>
    </div>
    <div>
      <p>
        A 2006 study by the scholars Mohammed Hafez and Joseph Hatfield examined
        rates of Hamas violence before and after such assassinations and
        concluded that their impact was negligible. Subsequent studies have
        reached similar conclusions. Targeted killings have barely affected the
        group’s capabilities or intentions. Yet in the wake of October 7, the
        Israeli government reached for the tactic again. A few weeks after the
        attack, Prime Minister Benjamin Netanyahu told reporters that Israel
        would “assassinate all the leaders of Hamas, wherever they are.” Ronen
        Bar, the chief of Israel’s internal intelligence agency, the Shin Bet,
        told members of the Israeli parliament that Israel would kill Hamas
        leaders “in Gaza, in the West Bank, in Lebanon, in Turkey, in Qatar,
        everywhere.” Since last October, Israel has reported killing over 100
        Hamas leaders, including some senior commanders in the group’s military
        wing.
      </p>
      <p>
        2006年，学者穆罕默德·哈菲兹(Mohammed Hafez)和约瑟夫·哈特菲尔德(Joseph
        Hatfield)研究了定点清除行动前后哈马斯暴力行为的频率，得出的结论是这些行动几乎没有什么影响。后来的研究得出了类似的结论。定点清除几乎没有影响该组织的能力或意图。然而，在10月7日袭击事件发生后，以色列政府再次诉诸这一手段。袭击发生几周后，总理本雅明·内塔尼亚胡(Benjamin
        Netanyahu)对记者说，以色列将“杀死所有哈马斯领导人，不管他们在哪里。”以色列国内情报机构辛贝特(Shin
        Bet)负责人罗恩·巴尔(Ron
        Bar)告诉以色列议员，以色列将杀死“在加沙、约旦河西岸、黎巴嫩、土耳其、卡塔尔各地”的哈马斯领导人。自去年10月以来，以色列报告已击毙100多名哈马斯领导人，包括该组织军事派系的一些高级指挥官。
      </p>
    </div>
    <div>
      <p>
        But these assassinations, although degrading Hamas’s military strength
        in Gaza, have not affected the group’s long-term capabilities; over the
        decades, it has demonstrated an ability to replace key leaders. And in
        addition to yielding few tactical gains, this approach has created
        strategic costs. When killing a leader may prevent an imminent attack,
        it is justified self-defense. But endless targeted killings not publicly
        connected to specific operations lead many observers to see a state’s
        actions as morally equivalent to those of the terrorist group itself.
        That is especially true the wider the list of targets grows: consider,
        for example, an Israeli airstrike in Gaza in April that killed three
        sons and four grandchildren of the Qatar-based Hamas political leader
        Ismail Haniyeh, which allowed him to portray himself not as a terrorist
        mastermind but as a grieving father and grandfather.
      </p>
      <p>
        但是，尽管定点清除削弱了哈马斯在加沙的军事实力，却并未影响该组织的长期能力；几十年来，哈马斯已证明其具有更换关键领导人的能力。而且，除了一些战术上的短期收益外，这种做法造成了战略损失。当刺杀能够阻止迫在眉睫的袭击时，它是自卫的正当手段。但是，如果无休止地进行并非明显与具体行动相关的定点清除，很多观察家就会将一国的行为与恐怖组织的行为等量齐观。目标名单越长，这一点就越明显。例如，4月，以色列对加沙发动空袭，炸死了哈马斯政治领导人哈立德·迈沙阿勒（Ismail
        Haniyeh）在卡塔尔的三个儿子和七个孙子。这反而让迈沙阿勒得以把自己塑造为一个悲痛欲绝的父亲和祖父，而非恐怖主义头目。
      </p>
    </div>
    <div>
      <h3>THE TALKING CURE</h3>
      <h3>《谈话疗法》</h3>
    </div>
    <div>
      <p>
        Instead of trying to kill Hamas leaders, Israel might try negotiating
        with them on a long-term political solution. That idea would be anathema
        to most Israelis, of course. And no one familiar with the long history
        of failed negotiations between the Israelis and the Palestinians—not to
        mention the profound anger that both groups currently feel—would be
        foolish enough to recommend peace talks now.
      </p>
      <p>
        与其试图除掉哈马斯领导人，以色列倒不如跟他们谈一谈有关长期政治解决方案的问题。当然，大多数以色列人对这一想法会嗤之以鼻。熟悉以色列人和巴勒斯坦人之间漫长而充满挫折的谈判历史、了解双方当前深深的愤怒情绪的人，也不会天真到建议现在就举行和谈。
      </p>
    </div>
    <div>
      <p>
        But negotiation does represent a fifth way that terrorism can end.
        Think, for example, of Northern Ireland, where the 1998 Good Friday
        Agreement ended the Provisional Irish Republican Army’s decades-long
        campaign of terrorism. In 2016, the Revolutionary Armed Forces of
        Colombia entered into a complex agreement with the government and agreed
        to disarm and operate as a normal political party. Like Hamas, those
        groups had enthusiastically murdered civilians. Talking to them was
        difficult for officials, and accepting former members back into society
        was hard for the public, especially the group’s victims and their
        families. But the bloodshed stopped, and in the end, states gave up
        relatively little.
      </p>
      <p>
        但谈判确实为终结恐怖主义提供了第五种方式。以北爱尔兰为例，1998年《耶稣受难日协议》结束了爱尔兰共和军长达数十年的恐怖主义运动。2016年，哥伦比亚革命武装力量与政府达成一项复杂协议，同意放下武器，作为一个正常政党开展活动。和哈马斯一样，这些组织都曾狂热地杀害平民。与它们进行谈判对官员来说很困难，社会接纳这些组织的前成员尤其令遇害者及其家人感到艰难。但暴力行为停止了，而国家最终付出的代价相对较小。
      </p>
    </div>
    <div>
      <p>
        Negotiations are risky for terrorist groups because showing up at the
        bargaining table gives away useful intelligence and undercuts the
        narrative that there is no alternative but to engage in violence. Only
        about 18 percent of terrorist groups ever negotiate at all, and talks
        usually drag on while violence continues, just at a lower level. Groups
        that have been around a long time are more likely to negotiate; the
        average lifespan of a terrorist group is eight to ten years, but groups
        that negotiate tend to have been around for 20 to 25 years.
      </p>
      <p>
        对恐怖组织来说，谈判是有风险的，因为坐在谈判桌旁会泄露有用的情报，也削弱了诉诸暴力别无选择的叙事。只有大约18%的恐怖组织会进行谈判，而谈判通常旷日持久，在此期间暴力行为虽然减少，但并未停止。存在时间较长的组织更有可能进行谈判；恐怖组织的平均寿命是8到10年，而进行谈判的组织往往已有20到25年的历史。
      </p>
    </div>
    <div>
      <p>
        Of course, there must be something tangible to negotiate over, and the
        most successful negotiations with terrorist groups involve conflicts
        over territory as opposed to religion or ideology. But even in the
        absence of an agreement, serious talks can cause divides within
        terrorist groups, splitting those who seek a political settlement from
        those still wedded to fighting. (On the other hand, negotiations
        sometimes prove futile: before moving to wipe out the LTTE, the Sri
        Lankan government spent more than five years negotiating with the group
        in talks brokered by Norway.)
      </p>
      <p>
        当然，谈判需要有具体内容，而与恐怖组织最成功的谈判涉及领土争端，而非宗教或意识形态。但即便谈判未能达成协议，认真谈判也会造成恐怖组织内部的分裂，把寻求政治解决方案的人与顽固坚持战斗的人区分开来。(不过，谈判有时并无结果：在彻底消灭泰米尔猛虎组织之前，斯里兰卡政府在挪威的斡旋下与该组织进行了长达五年的谈判。)
      </p>
    </div>
    <div>
      <p>
        Negotiations may not seem a likely way for Hamas to end. For one thing,
        the group has a long history of scorning talks with Israel. In the
        1990s, it would engage in spoiler attacks when it believed the peace
        process was making progress. And today, Hamas is more committed than
        ever to pursuing a variant of the so-called one-state solution that
        would involve obliterating the other side, as are some Israeli
        extremists.
      </p>
      <p>
        谈判看起来并不是哈马斯终结的可能方式。毕竟，该组织有着长期拒绝与以色列会谈的历史。在上世纪90年代，当它认为和平进程取得进展时，就会发动破坏活动。如今，哈马斯比以往任何时候都更加致力于实现一种所谓的“一个国家”解决方案，即消灭另一方，这与某些极端以色列人并无二致。
      </p>
    </div>
    <div>
      <p>
        Still, Hamas and Israel have conducted negotiations in the past,
        generally through intermediaries such as Qatar—including talks that led
        to a short cease-fire and an exchange of hostages and prisoners last
        November. It seems possible that external actors such as the United
        States, Egypt, Jordan, and Saudi Arabia might eventually find a way to
        push Israel and the Palestinians into a renewed diplomatic process aimed
        at creating a two-state solution. And it is possible to imagine Hamas,
        or at least some faction or remnant of the group, being involved in some
        way. Such negotiations would be long, fraught, and hamstrung by
        extremists on both sides. But merely announcing a process would have
        salutary effects. Indeed, it could even create the conditions for what
        might be the most likely way for Hamas’s terrorism to end: self-defeat.
      </p>
      <p>
        巴勒斯坦伊斯兰抵抗运动(哈马斯)和以色列此前确实进行过谈判，通常是通过卡塔尔等中间人进行的，包括去年11月达成短暂停火和交换战俘的谈判。可以想像，在美国、埃及、约旦和沙特阿拉伯等外部力量的推动下，以色列人和巴勒斯坦人或许最终能够恢复旨在建立两个国家的外交进程。甚至可以想像哈马斯——或其某个派系或残余力量——以某种方式参与其中。这类谈判将旷日持久，充满困难，并受到双方极端主义者的阻碍。但仅仅宣布启动这一进程就会产生有益影响。事实上，这甚至可能创造出终结哈马斯恐怖主义的最有可能的方式：自取灭亡。
      </p>
    </div>
    <div>
      <h3>THEIR OWN WORST ENEMIES</h3>
      <h3>他们自己就是最坏的敌人</h3>
    </div>
    <div>
      <p>
        Most terrorist groups end in a sixth way: because they fail, either by
        collapsing in on themselves or by losing support. Groups that implode
        sometimes die out during generational shifts (the far-left Weather
        Underground in the United States from the 1960s to the 1980s),
        disintegrate into factions (remnants of the IRA after the Good Friday
        Agreement), break down over operational disagreements (the Front de
        Libération du Québec, a Canadian separatist group, in the early 1970s),
        or fracture over ideological differences (the communist Japanese Red
        Army in 2001).
      </p>
      <p>
        大多数恐怖组织以第六种方式结束：失败，无论是因为自身垮台还是失去民众支持。有些组织在代际更迭中土崩瓦解（比如美国二十世纪六、七十年代的极左翼“气象地下组织”）、分裂成各个派系（《耶稣受难日协议》之后的爱尔兰共和军残部）、因行动分歧而瓦解（二十世纪七十年代初的加拿大魁北克分离主义组织“魁北克解放阵线”），或者因为意识形态分歧而四分五裂（二十一世纪初的日本赤军）。
      </p>
    </div>
    <div>
      <p>
        Groups also fail because they lose popular support. Sometimes, that is
        because governments offer members a better alternative, such as amnesty
        or jobs. But by far the most important reason terrorist groups fail is
        that they miscalculate, especially by making targeting errors that stir
        revulsion among important constituencies. The Real IRA’s August 1998
        bombing of Omagh, a small market town in Northern Ireland, killed 29
        people, including a number of children. Widespread disgust at the attack
        unified disparate parts of society and solidified support for the Good
        Friday Agreement. Chechen separatists made a similar mistake in 2004
        when they seized a school in Beslan, Russia, leading to the deaths of
        more than 300 people, including almost 200 children, and sparking a
        near-total collapse of support for the separatist cause inside Chechnya
        and throughout Europe. The following year, suicide bombers belonging to
        al Qaeda in Iraq (the forerunner of ISIS) attacked three hotels in
        Amman, Jordan, killing around 60 people. Opinion polls later showed
        that, in the aftermath, 65 percent of Jordanians changed their view of
        al Qaeda from positive to negative. (Historically, at least a third of
        al Qaeda’s victims have been Muslims, which is the main reason that the
        group has not become the popular movement that Osama bin Laden hoped it
        would be.)
      </p>
      <p>
        许多组织之所以失败，是因为失去了民众的支持。有时候，这是因为政府为成员提供了更好的出路，比如赦免或工作机会。但更为重要的是，恐怖组织往往误判形势，尤其是在目标选择上犯下令人愤慨的错误，从而疏远了关键群体。1998年8月，真实爱尔兰共和军在北爱尔兰小城奥马赫制造爆炸，炸死29人，其中包括多名儿童。这起袭击事件激起普遍的反感，团结了社会各阶层，进一步巩固了对《星期五协议》的支持。2004年，车臣分裂分子在俄罗斯别斯兰市一所学校制造人质事件，导致300多人死亡，近200名儿童丧生，从而几乎彻底丧失了车臣和整个欧洲对分裂事业的支持。一年后，“伊拉克基地组织”（ISIS的前身）的自杀式炸弹袭击者在约旦安曼的三家酒店发动袭击，造成大约60人死亡。民调显示，事后有65%的约旦人改变了对基地组织的看法，从支持转为反对。（从历史来看，基地组织至少有三分之一的受害者是穆斯林，这也是该组织未能成为奥萨马·本·拉登希望的那种大众运动的主要原因。）
      </p>
    </div>
    <div>
      <p>
        Hamas has all the ingredients of a group that can fail. Most important,
        perhaps, is the fact that it is not popular. Shortly after the group
        took control of Gaza in 2007, Palestinian support for Hamas began to
        deteriorate. According to polling by the Pew Research Center, 62 percent
        of people in the Palestinian territories had a favorable view of Hamas
        in 2007. By 2014, only a third did. Khalil Shikaki, a Palestinian
        political scientist and pollster, has found that support for Hamas
        generally spikes during confrontations with Israel but then dissipates
        when the group fails to deliver positive change.
      </p>
      <p>
        哈马斯具备失败的所有条件。也许最重要的是，它不得人心。2007年该组织接管加沙之后不久，巴勒斯坦民众对它的支持就开始减弱。皮尤研究中心(Pew
        Research
        Center)的民调显示，2007年，62%的巴勒斯坦人对哈马斯持正面看法。到2014年，这一比例仅为三分之一。巴勒斯坦政治学家、民调专家哈利尔•希凯基(Khalil
        Shikaki)发现，巴勒斯坦民众对哈马斯的支持率通常会在与以色列冲突期间上升，但当哈马斯未能带来积极变化时，这种支持率又会消退。
      </p>
    </div>
    <div>
      <p>
        Israel’s excessive use of military force, however, has strengthened
        Hamas’s hold and aided the group’s propaganda about what happened on
        October 7. According to a poll that Shikaki conducted in March, 90
        percent of Palestinians dismiss the idea that Hamas engaged in war
        crimes that day. Any revulsion that ordinary Gazans might have felt
        about what Hamas did in their name was likely overwhelmed by their
        horror over what Israel has done to their loved ones, homes, and cities.
      </p>
      <p>
        然而，以色列过度使用武力却巩固了哈马斯的地位，也帮了该组织一个忙——关于10月7日发生的事情，哈马斯的宣传如今已无人质疑。根据希卡基3月份进行的一项调查，90%的巴勒斯坦人认为哈马斯在那天并没有犯下战争罪行。对于以自己名义行事的哈马斯，加沙普通民众或许确实感到厌恶，但这种厌恶情绪很可能被他们对以色列残害亲人、摧毁家园和城市行为的恐惧所压倒。
      </p>
    </div>
    <div>
      <p>
        Still, Hamas has fissures that could widen and even lead to its
        collapse. Its military and political leadership are not always in sync:
        according to The New York Times, the group’s Gaza-based military leader,
        Yahya Sinwar, launched the October 7 attacks with a handful of military
        commanders, keeping Hamas’s political leader, Haniyeh, in the dark until
        just a few hours before the operation began. Reporting by Reuters
        revealed that some Hamas leaders seemed shocked by the timing and scale
        of the attacks. The group also faces pressure and competition from
        Palestinian Islamic Jihad, which is smaller than Hamas but more closely
        aligned with Iran. And with much of Hamas’s organization in Gaza
        destroyed, other power structures, including clans and even criminal
        networks, could vie for control and undercut the group.
      </p>
      <p>
        然而，哈马斯内部的裂痕可能会扩大，甚至导致其垮台。哈马斯的军事和政治领导层并不总是同步的:据《纽约时报》报道，该组织驻加沙的军事领导人叶海亚辛瓦尔(Yahya
        Sinwar)与少数几名军事指挥官一起发动了10月7日的袭击，直到行动开始前几个小时，哈马斯的政治领导人哈尼亚(Haniyeh)才被蒙住了。据路透社报道，一些哈马斯领导人似乎对袭击的时机和规模感到震惊。哈马斯还面临来自巴勒斯坦伊斯兰圣战组织的压力和竞争。巴勒斯坦伊斯兰圣战组织的规模比哈马斯小，但与伊朗关系更密切。随着哈马斯在加沙的大部分组织被摧毁，其他权力结构，包括部族甚至犯罪网络，可能会争夺控制权，削弱该组织。
      </p>
    </div>
    <div>
      <p>
        But the far more likely way that Hamas could fail is through popular
        backlash. Hamas rules Gaza through oppression, using arrests and torture
        to suppress dissent. Gazans widely loathe its internal General Security
        Service, which surveils and keeps files on people, stamps out protests,
        intimidates journalists, and tracks people accused of “immoral acts.”
        Since October 7, many Palestinians have expressed anger at Hamas for
        having misjudged the consequences of the attack—a serious targeting
        error that has indirectly led to the deaths of tens of thousands of
