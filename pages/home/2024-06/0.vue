        哈马斯对以色列的袭击以及以色列的回击对平民来说是一场灾难。10月7日的大屠杀中，哈马斯袭击了包括妇女、儿童和老人在内的手无寸铁的以色列平民，造成近1200人死亡，并有大约240人被扣为人质。随后，以色列在加沙地带的空袭和地面进攻行动（截至2024年3月）已造成3万多人死亡，其中估计有三分之二是妇女和儿童。以色列的攻势还使约200万人（占加沙地带总人口的85%以上）流离失所，100多万人面临饥饿风险，15万座民用建筑遭到破坏或摧毁。如今，加沙北部已没有一座能正常运作的医院。以色列坚称，哈马斯利用民用设施作为掩体，在这些设施内活动或在它们下方挖掘地道——也许正因为根据国际法，这些民用建筑是军事行动的禁区。
      </p>
    </div>
    <div>
      <p>
        International humanitarian law, also known as the law of war or the law
        of armed conflict, is supposed to spare civilians from the worst
        calamities of conflict. The aim of this body of law has always been
        clear: civilians not involved in the fighting deserve to be protected
        from harm and to enjoy unimpeded access to humanitarian aid. But in the
        Israel-Hamas war, the law has failed. Hamas continues to hold hostages
        and has used schools, hospitals, and other civilian buildings to shield
        its infrastructure, while Israel has waged an all-out war in densely
        populated areas and slowed the flow of desperately needed aid to a
        trickle. The result has been utter devastation for civilians in Gaza.
      </p>
      <p>
        国际人道主义法(也称战争法或武装冲突法)本应保护平民免遭冲突最糟糕的灾难。这部法律的目的一直很明确：不参与战斗的平民有权免受伤害，有权获得不受阻碍的人道主义援助。但在以色列-哈马斯战争中，法律失败了。哈马斯继续扣押人质，并利用学校、医院和其他民用建筑来保护自己的基础设施；而以色列则在人口密集地区全面开战，使得急需的救援物资缓慢得像涓涓细流。结果就是加沙平民遭受彻底的毁灭。
      </p>
    </div>
    <div>
      <p>
        The conflict in Gaza is an extreme example of the breakdown of the law
        of war, but it is not an isolated one. It is the latest in a long series
        of wars in the years since 9/11, from the U.S.-led “war on terror” to
        the Syrian civil war to Russia’s war in Ukraine, that have chipped away
        at protections for civilians. From this grim record, it might be
        tempting to conclude that the humanitarian protections that governments
        worked so hard to enshrine in law after World War II hold little meaning
        today. Yet even a hobbled system of international humanitarian law has
        made conflict more humane. Indeed, for all the frequent transgressions,
        the existence of these legal protections has provided continuous
        pressure on belligerents to limit civilian casualties, provide safe
        zones for noncombatants, and allow for humanitarian access—knowing they
        will face international consequences when they do not.
      </p>
      <p>
        加沙冲突是战争法彻底失灵的极端例子，但并非个案。从美国领导的“反恐战争”，到叙利亚内战，再到俄罗斯在乌克兰的战争，在9·11事件后的这些年里，一系列的冲突都在侵蚀对平民的保护。面对这一悲惨记录，人们或许会认为，二战后各国政府竭力将之载入法律的人道主义保护措施如今已经没有什么意义了。即便如此，国际人道主义法律体系即便残缺不全，也使得战争变得更人道一些。事实上，尽管各方经常违反相关规定，但这些法律保护的存在持续对交战各方构成了压力，迫使其限制平民伤亡，为非战斗人员设立安全区，并允许人道主义援助——它们深知，一旦不这样做，就要面对国际后果。
      </p>
    </div>
    <div>
      <p>
        After the horrors of World War II, the United States and its allies
        established the Geneva Conventions, the four treaties of 1949 that lay
        out elaborate rules governing the conduct of war. At a moment when the
        laws of war are once again being severely tested, the United
        States—which, especially in the years after 9/11, helped weaken
        them—should act now to renew and strengthen them.
      </p>
      <p>
        第二次世界大战的恐怖经历之后，美国及其盟友制定了1949年四项《日内瓦公约》，为战争行为制定了详尽的规则。如今，战争法则再次受到严峻考验，而美国尤其应当带头重申并强化这些法则——要知道，在“9·11”事件后的岁月里，美国对这些法则造成了严重削弱。
      </p>
    </div>
    <div>
      <h3>LICENSE TO KILL</h3>
      <h3>杀人执照</h3>
    </div>
    <div>
      <p>
        The law of war offers a tradeoff. Soldiers of a sovereign nation can be
        lawfully killed in armed conflict. In exchange, they are granted
        immunity that allows them to commit acts that in any other context would
        likely be considered crimes—not only to kill but also to trespass, break
        and enter, steal, assault, maim, kidnap, destroy property, and commit
        arson. This immunity applies whether their cause is just or unjust.
      </p>
      <p>
        战争法是一种权衡。交战国的士兵在武装冲突中可以合法地被杀。作为交换，他们被授予一种豁免权，允许他们实施在其他任何情况下都可能被视为犯罪的行为??不仅仅是杀人，还包括非法侵入、盗窃、袭击、伤害、绑架、破坏财产和纵火——不管他们的事业是否正义。
      </p>
    </div>
    <div>
      <p>
        There are limits—which, for most of history, were modest. Hugo Grotius,
        the early-seventeenth-century Dutch diplomat who has been called “the
        father of international law,” wrote that soldiers should be prohibited
        from using poison, killing by deception (for example, after feigning
        surrender), and rape. In Grotius’s framework, these three offenses made
        up the only exceptions to a soldier’s license to kill. Enslavement,
        torture, pillaging, and the execution of prisoners were all allowed; so
        was the intentional killing of unarmed civilians, including women and
        children. Although few treaties governed the conduct of war at the time,
        countries in western Europe widely accepted these rules as customary
        international law.
      </p>
      <p>
        这些限制，在历史上大多数时候都很有限。17世纪早期的荷兰外交官雨果·格劳秀斯(Hugo
        Grotius)被称为“国际法之父”，他写道，应该禁止士兵使用毒药、通过欺骗杀人（例如假装投降之后行凶）和强奸。在格劳秀斯看来，这三种行为是士兵杀人特权的唯一例外。奴役、折磨、劫掠和处决战俘都是允许的；蓄意杀害包括妇女和儿童在内的手无寸铁的平民也是可以的。当时几乎没有多少条约规定战争行为，但西欧各国普遍将这些规则视为习惯国际法。
      </p>
    </div>
    <div>
      <p>
        According to Grotius, soldiers were not allowed to massacre civilians
        whenever they liked. They were legally permitted to take the steps
        necessary to enforce the rights on which the enemy had infringed—and
        nothing more. If killing women and children did not advance the war
        effort, there was no justification for doing so. Yet even if the
        senseless slaughter of innocent civilians was technically illegal under
        international law at the time, those who committed it could not be held
        accountable; such deeds, Grotius observed, could be “made with
        impunity.” The lack of legal remedy for attacks on civilians began to be
        addressed only in the middle of the eighteenth century, when countries
        gradually adopted the principle of distinction, which requires soldiers
        to distinguish between combatants and civilians.
      </p>
      <p>
        根据格劳秀斯的理论，士兵并不能随心所欲地屠杀平民。他们只能采取必要措施来维护敌人侵犯的权利——仅此而已。如果杀害妇女和儿童无助于战争的进行，那么这种行为就得不到任何辩解。然而，即便当时国际法严格禁止滥杀无辜，实施此类暴行的人却不会受到惩罚；格劳秀斯注意到，这类行为“可以逍遥法外”。直到18世纪中叶，各国逐渐采纳区分战斗员和非战斗员的原则，对平民的袭击才开始有了法律上的补救措施。
      </p>
    </div>
    <div>
      <p>
        The rules governing war continued to evolve over the course of the
        nineteenth century. The first Geneva Convention, signed in 1864,
        prohibited attacks on hospitals, medical personnel, and their patients.
        The 1868 St. Petersburg Declaration banned the use of fragmenting,
        explosive, or incendiary small-arms munitions. The 1899 and 1907 Hague
        Conventions, ratified by most world powers at the time, prohibited
        attacking towns and buildings that were not defended by military forces.
        They also banned pillaging, executing prisoners of war, and compelling
        civilians to swear allegiance to a foreign power.
      </p>
      <p>
        战争法则在19世纪继续演变。1864年签订的第一部《日内瓦公约》禁止攻击医院、医护人员及其病人。1868年的圣彼得堡宣言禁止使用爆炸性或燃烧性小型武器弹药。1899年和1907年的《海牙公约》（大多数世界大国当时都批准了这两项公约）禁止袭击没有军事防御的城镇和建筑，还禁止劫掠、处决战俘以及强迫平民对外国宣誓效忠。
      </p>
    </div>
    <div>
      <p>
        But countries that were engaged in war struggled to figure out how to
        enforce these rules. Their solution was generally reprisal: if an
        adversary violated the laws of war in a military operation, a country
        would respond with a violation of its own. Often, the reprisals would be
        meted out on prisoners of war, who were near at hand and could easily be
        killed. But civilians were not insulated from attacks. When Spanish
        guerrillas attacked a French column in Spain’s Sil Valley in 1808,
        during the Napoleonic Wars, the French commanding officer, General
        Louis-Henri Loison, ordered his soldiers to torch the countryside.
      </p>
      <p>
        但那些卷入战争的国家却苦于不知道如何实施这些规则。它们通常采取报复行动：如果敌方在军事行动中违反了战争法，另一方就会以违反战争法作为回应。而最常成为报复对象的，就是近在咫尺、易于处死的战俘。但平民并未得到保护。1808年，在拿破仑战争期间，西班牙游击队袭击了法国驻西里谷的一支部队，法军指挥官路易-亨利·洛斯将军命令部下放火烧毁周边地区。
      </p>
    </div>
    <div>
      <h3>THE POSTWAR RECKONING</h3>
      <h3>战后清算</h3>
    </div>
    <div>
      <p>
        During World War II, more than 30 million civilians were killed. In the
        aftermath of such catastrophic violence, it was clear that new and
        stronger rules were needed to regulate war. In 1949, a series of
        international conferences convened by the International Committee of the
        Red Cross established the four Geneva Conventions in an effort to
        prevent the most brutal violence of war. Although Grotius offered just
        three prohibitions to guide states in war, the Geneva Conventions and,
        later, its three Additional Protocols filled hundreds of pages with
        specific rules for almost any scenario. The new rules governed the
        treatment of wounded and sick military personnel in the field and at
        sea, prisoners of war, and civilians.
      </p>
      <p>
        在第二次世界大战期间，有超过3000万平民丧生。如此灾难性的暴力行为之后，人们清楚地意识到，需要新的、更强大的规则来管理战争。1949年，红十字国际委员会召集了一系列国际会议，制定了四项《日内瓦公约》，以防止战争中最为残暴的暴力行为。与格劳秀斯仅提供三条指导国家战争行为的禁令不同，《日内瓦公约》及其后来的三项《附加议定书》对几乎任何可能的情形都规定了具体规则，篇幅长达数百页。这些新规则规定了如何对待陆地和海上的伤者和病人、战俘以及平民。
      </p>
    </div>
    <div>
      <p>
        Unlike the early laws of war, the Geneva Conventions prohibited not just
        senseless violence but also some forms of violence that advanced war
        aims. To adhere to the conventions, parties to a conflict must
        distinguish between civilians and combatants and between civilian places
        and military ones. Above all, they may never intentionally target
        civilians or “civilian objects,” such as schools, private homes,
        construction equipment, businesses, places of worship, and hospitals
        that do not directly contribute to military action. And civilians must
        never be the target of reprisals. The principle of proportionality,
        codified in 1977 in Additional Protocol I, acknowledges that sometimes
        armies will harm civilians and civilian objects when pursuing military
        objectives. But the rule requires that the damage not be “excessive in
        relation to the concrete and direct military advantage anticipated.” The
        principle of precaution, moreover, requires that armies must take
        constant care to spare civilians and civilian objects, even if doing so
        might slow down military operations.
      </p>
      <p>
        与早期的战争法不同，日内瓦公约不仅禁止无意义的暴力行为，还禁止某些能实现战争目标的暴力行为。为了遵守这些公约，冲突各方必须区分平民和战斗员，区分民用设施和军事设施。最重要的是，它们决不能故意攻击平民或“民用物体”，比如并未直接参与军事行动的学校、私人住宅、建筑设备、企业、礼拜场所和医院。平民也决不能成为报复的目标。1977年《第一附加议定书》编纂的相称性原则承认，军队在追求军事目标时有时会伤害到平民和民用物体。但根据该原则，这类伤害“不应超过预期的并直接的军事利益”。此外，谨慎原则要求军队必须时刻注意保护平民和民用物体，即便这可能会放慢军事行动的速度。
      </p>
    </div>
    <div>
      <p>
        The Geneva Conventions, their protocols, and the customary international
        law that has grown around them take an important step beyond the rules
        that came before. They aim to protect civilians from harm even when that
        harm might serve a strategic purpose. Thus, an attack on a military
        target that would help a belligerent’s war effort is prohibited if it
        would hurt too many civilians.
      </p>
      <p>
        《日内瓦公约》及其附加议定书以及围绕这些文书发展起来的国际习惯法向前迈出了一大步，它们试图保护平民免受伤害，即使这种伤害可能符合某种战略目的。因此，如果袭击会伤害过多平民，那么即便是有利于交战一方军事行动的打击也是被禁止的。
      </p>
    </div>
    <div>
      <p>
        In many ways, the Geneva Conventions have been remarkably successful.
        All four conventions have been ratified by all UN member states. Most
        countries have adopted military manuals that translate the conventions
        into concrete rules meant to guide the conduct of their armies. Many
        have enforced these rules against their own soldiers. Yet these
        elaborate and ambitious rules were shaped by wars that were very
        different from most conflicts today.
      </p>
      <p>
        在许多方面，日内瓦公约取得了令人称道的成功。所有四个公约都得到了联合国所有成员国的批准。大多数国家都制定了军事手册，把公约转化为旨在指导本国军队行为的具体规则。很多国家还对本国士兵执行了这些规则。然而，这些详尽而雄心勃勃的规则是由与今天大多数冲突截然不同的战争塑造的。
      </p>
    </div>
    <div>
      <p>
        Since the end of World War II, wars between states have sharply
        declined, but conflicts involving nonstate armed groups have risen. The
        Geneva Conventions say little about the latter. Only one article, Common
        Article 3, specifically applies to wars with nonstate groups. Protecting
        civilians in war, it turns out, is much harder when one of the
        belligerents is a nonstate actor. Combatants belonging to nonstate
        groups generally don’t wear uniforms. Although their members may
        assemble, train in camps, and be organized under a hierarchical
        leadership, they tend to operate in places where civilians are also
        present. As a result, it can be extremely difficult to tell them apart
        from ordinary civilians.
      </p>
      <p>
        自二战结束以来，国家间的战争已经大幅减少，但涉及非国家武装团体的冲突却有所增加。然而，《日内瓦公约》对这类冲突着墨不多。只有共同第三条专门适用于与非国家团体的战争。事实证明，当冲突一方是非国家行为体时，保护战时平民的工作要艰巨得多。非国家团体的战斗人员通常不穿制服。尽管其成员可能集结、在营地训练，并在等级森严的领导层下运作，但他们往往在有平民的地方活动。因此，很难将他们与普通平民区分开来。
      </p>
    </div>
    <div>
      <h3>SELF-DEFENSE CLASSES</h3>
      <h3>自卫课程</h3>
    </div>
    <div>
      <p>
        The 9/11 attacks and the U.S. response to them inaugurated a new era of
        war that has pushed international humanitarian law to a breaking point.
        Before 2001, legitimate self-defense under international law was
        generally understood to apply only when one country was defending an
        attack from another. Until then, few countries had cited nonstate actors
        as their primary reason for using force in self-defense. (Israel was a
        notable exception; its adversaries included irregular forces located in
        Egypt, Jordan, Lebanon, and Syria.)
      </p>
      <p>
        “9·11”事件及其引发的美国反恐战争开创了一个新时代，将国际人道法推至极限。在2001年之前，各国普遍认为，国际法意义上的合法自卫仅限于一国抵御来自另一国的攻击。在此之前，很少有国家将非国家行为体作为实施自卫动武的主要理由。（以色列是个明显的例外：它的对手包括位于埃及、约旦、黎巴嫩和叙利亚境内的非常规力量。）
      </p>
    </div>
    <div>
      <p>
        After 9/11, self-defense claims changed. The United States justified its
        invasion of Afghanistan by arguing that it was responding to, as the
        Bush administration informed the UN Security Council, the “ongoing
        threat to the United States and its nationals posed by the Al-Qaeda
        organization.” Within a year, Australia, Canada, France, Germany, New
        Zealand, Poland, and the United Kingdom had also filed claims of
        self-defense against al Qaeda. And it was not long before countries
        began making claims against other nonstate groups. In 2002, for example,
        Rwanda cited a right of self-defense against the Interahamwe, a militia
        group. And in 2003, Côte d’Ivoire cited the same right against “rebel
        forces.”
      </p>
      <p>
        9·11事件之后，自卫的主张发生了变化。美国以对美国及其国民构成持续威胁的基地组织为由，向联合国安理会解释其入侵阿富汗之举是正当的。一年之内，澳大利亚、加拿大、法国、德国、新西兰、波兰和英国也都提出了针对基地组织的自卫主张。不久，各国开始针对其他非国家行为体提出自卫主张。例如，2002年，卢旺达援引了对Interahamwe民兵组织的自卫权。2003年，科特迪瓦援引了针对“叛军”的同样权利。
      </p>
    </div>
    <div>
      <p>
        To confront groups such as al Qaeda and the Islamic State (also known as
        ISIS), the United States and its allies came to rely on what they dubbed
        the “unwilling or unable doctrine”—the theory that action against a
        nonstate threat is justified as long as the country in which the
        nonstate actor is found is unwilling or unable to suppress the threat.
        In most cases, the United States sought the consent of governments to
        target nonstate actors in their territories. Iraq, Somalia, Yemen, and,
        while the Taliban was out of power, Afghanistan all agreed to U.S.
        intervention. When states would not consent—for example, Syria—the
        United States used the unable or unwilling theory, explicitly endorsed
        by fewer than a dozen countries, to justify using military force.
      </p>
      <p>
        为了打击“基地”组织和“伊斯兰国”(Islamic State,
        也称ISIS)等团体，美国及其盟友开始诉诸他们所谓的“不情愿或无能力”理论——只要非国家行为体所在国家不情愿或无法消除威胁，就有理由对其采取行动。在大多数情况下，美国会寻求所在国政府同意其在本国领土上针对非国家行为体采取行动。伊拉克、索马里、也门以及塔利班垮台后的阿富汗都同意美国介入。如果国家不同意——比如叙利亚——美国就会援引只有大约一打国家明确支持的“不情愿或无能力”理论来为其使用武力正名。
      </p>
    </div>
    <div>
      <p>
        As Washington went to war with nonstate actors, it struggled with how to
        distinguish the civilians it was allowed to kill according to the Geneva
        Conventions—those “who take a direct part in hostilities”—from those it
        was not. If a civilian who was not a member of ISIS performed a task for
        the group—say, placing an improvised explosive device on a road—and then
        returned to work as an ordinary laborer, could that person still be
        targeted?
      </p>
      <p>
        当美国对非国家行为体开战时，它在如何区分根据《日内瓦公约》允许杀害的平民（即“直接参与敌对行动者”）和不允许杀害的平民方面遇到困难。如果一名不属于ISIS的平民为该组织执行了一项任务——比如，在公路上放置简易爆炸装置——然后回去做一名普通工人，此人是否仍可成为打击目标？
      </p>
    </div>
    <div>
      <p>
        In 2009, the International Committee of the Red Cross issued guidance to
        governments on how to protect civilians when fighting nonstate actors.
        The ICRC document reiterated the rule that civilians must be protected
        against direct attack “unless and for such time as they take direct part
        in hostilities.” It set out the principle that civilians who do not take
        a direct part in hostilities must be distinguished not only from armed
        forces but also from those who participate in hostilities “on an
        individual, sporadic or unorganized basis only.” The devil was very much
        in the details.
      </p>
      <p>
        2009年，红十字国际委员会(ICRC)发布了一份指导文件，指导各国政府如何在打击非国家行为体的战斗中保护平民。ICRC文件重申了一项规则，即平民必须受到保护，不得成为“直接攻击的目标，除非并直到他们直接参与敌对行动”。它阐述了如下原则：没有直接参与敌对行动的平民必须不仅与武装部队区分开来，而且也要与那些仅“个别、零星或无组织地”参与敌对行动的人区分开来。魔鬼就藏在细节里。
      </p>
    </div>
    <div>
      <p>
        The ICRC concluded that direct participation in hostilities “refers to
        specific acts carried out by individuals as part of the conduct of
        hostilities between parties to an armed conflict.” A person integrated
        into an organized armed group has a “continuous combat function” and can
        be targeted throughout the war. Hence, ISIS fighters are considered
        legitimate military targets as long as the conflict with ISIS continues.
        But ISIS members who provide noncombat support, including recruiters,
        trainers, and financiers, are not. A civilian who places an improvised
        explosive device for ISIS is directly participating in the war when
        positioning the weapon and while in transit for the task. But once this
        task is finished, so is the direct participation in the war, and the
        person can no longer be targeted. Many countries rejected the ICRC’s
        guidance, including the United States and the United Kingdom, which came
        up with their own rules for their counterterrorism campaigns in the
        Middle East.
      </p>
      <p>
        红十字国际委员会认为，“直接参与敌对行动”是指“交战方在武装冲突中进行敌对行为时，个人所从事的特定行为”。加入有组织的武装团体的人具有“持续战斗职能”，在整个战争期间都可以被当作目标。因此，只要与ISIS的战争还在继续，ISIS战士就一直被视为合法的军事目标。但为ISIS提供非战斗支持的成员，包括招聘者、教官和资助者，则不属于这一类。为ISIS放置简易爆炸装置的平民，在放置武器以及执行这项任务的往返途中，是在直接参与战争。但一旦任务完成，他就不再直接参与战争，不能再被当作目标。很多国家拒绝接受红十字国际委员会的指导，其中包括美国和英国，它们为在中东开展反恐运动制定了自己的规则。
      </p>
    </div>
    <div>
      <h3>BLURRED LINES?</h3>
      <h3>模糊的界線？</h3>
    </div>
    <div>
      <p>
        To address the changing reality of urban combat, the United States and
        other countries adopted new policies that once more put civilians in the
        cross hairs. At the center of this shift was the concept of so-called
        dual-use objects. According to international humanitarian law, all sites
        are either military or civilian; there is nothing in between. Objects
        normally dedicated to civilian purposes, such as places of worship,
        houses, or schools, are presumed to be civilian. But they can lose their
        civilian status if they are used for a military purpose.
      </p>
      <p>
        为了应对城市战争的新现实,美国和其他国家采取了新政策,再次将平民置于危险之中。这一变化的核心是这样一种观念,即所谓“双重用途”物体。根据国际人道法,所有地点要么是军事的,要么是民用的;没有中间地带。教堂、住宅或学校等通常用于民用目的的场所假定为民用。但若它们被用于军事目的,就会失去民用地位。
      </p>
    </div>
    <div>
      <p>
        The clear-cut division between civilian and military often fails to
        match the reality on the ground. There are many sites and structures
        that serve important civilian purposes but, by virtue of having some
        military use, may be considered military objectives—for example, trains,
        bridges, power stations, and communications infrastructure. Even an
        apartment building, if part of it serves for weapons storage, can be
        considered dual use.
      </p>
      <p>
        清晰的军民区分往往与实际情况不符。许多设施和结构虽然有重要的民用功能，但由于具有某种军事用途，可能被认定为军事目标，例如火车、桥梁、发电站和通讯设施。甚至一栋公寓楼，如果其中一部分用作武器储存，也可视为具有双重用途。
      </p>
    </div>
    <div>
      <p>
        More controversially, the United States now considers sectors of the
        adversary’s economy that may help sustain a war as legitimate targets.
        In the course of its operations against ISIS, for example, the United
        States struck oil wells, refineries, and tanker trucks. States generally
        agree that industries directly related to the military or defense may be
        targeted, such as those producing arms or supplying fuel to military
        vehicles. But they diverge on whether a belligerent may target an
        industry that contributes only indirectly to military activities, by
        providing financial support, for example. The Department of Defense Law
        of War Manual maintains that a given industry’s or sector’s “effective
        contribution to the war-fighting or war-sustaining capability of an
        opposing force is sufficient.” This means that banks, businesses, and,
        indeed, any source of economic activity that contributes to an
        adversary’s ability to sustain itself could be fair game. And because
        members of nonstate groups often rely on the same sources as ordinary
        civilians for food, fuel, and money, these areas of the economy that are
        essential to civilian life are regularly in the direct line of fire.
      </p>
      <p>
        更具争议的是，美国现在认为，有助于敌人持续作战的经济部门也是合法目标。例如，在对“伊斯兰国”(ISIS)的行动中，美国打击了油田、炼油厂和油罐车。各国普遍同意，直接与军事或国防相关的行业可以成为目标，比如生产武器或为军车提供燃料的行业。但在是否可以攻击那些仅间接支持军事活动的行业——例如通过提供财政支持——的问题上，它们意见不一。美国国防部《战争法手册》(Department
        of Defense Law of War
        Manual)坚持认为，某个行业或部门“对敌方部队作战或维系作战能力的实际贡献就足够了”。这意味着，银行、企业乃至任何有助于敌人维持自身能力的经济活动来源都可能成为合理目标。由于非国家行为体的成员往往依赖与平民相同的来源获取食物、燃料和金钱，这些对平民生活至关重要的经济领域经常首当其冲。
      </p>
    </div>
    <div>
      <p>
        As a result, the dual-use concept has increasingly made a wide variety
        of civilian activities subject to potential military action. An
        enterprise that is mostly used for civilian purposes, such as an oil
        refinery or even a bakery, can become a target in war if it contributes
        in some way to the war effort. It is still the case that harm to
        civilians and civilian infrastructure must be proportional to the
        potential military advantage attained. But the United States and Israel
        take the position that any site that can plausibly qualify as dual use
        is a legitimate military objective. Damage to such a target, then, is
        not part of the proportionality calculus. If noncombatant civilians are
        expected to be harmed, that must be weighed before taking the strike,
        but the long-term loss of vital civilian services, such as those
        provided by a water treatment plant, an electric grid, a bank, or a
        hospital, does not.
      </p>
      <p>
        于是，越来越多的民用活动落入了可能受到军事打击的范围。“两用”概念意味着，一家炼油厂，甚至一家面包房——只要它以某种方式有助于战争努力——都可能成为战争目标，尽管它的主要用途是民用。对平民和民用基础设施的伤害仍然必须与所获得的潜在军事优势成比例。但美国和以色列认为，任何可能具有双重用途的设施都是合法的军事目标。因此，打击这类目标造成的损害，并不算是衡量是否比例的原则之一。如果预期会伤害到非战斗人员平民，那么这必须在发动袭击之前加以考虑；但长期失去重要民用服务——比如水处理厂、电网、银行或医院提供的服务——则不必考虑在内。
      </p>
    </div>
    <div>
      <p>
        The military logic behind Israel’s air and ground campaign in Gaza is,
        in part, a result of these incremental changes, which both the United
        States and Israel have contributed to for decades. Hamas is both a
        nonstate actor and the de facto governing authority in Gaza. Determining
        who is a Hamas fighter and who is not, particularly from the air, is
        difficult. Even on the ground, Israeli forces have often failed to
        distinguish between civilians and combatants, as in December 2023, when
        Israeli troops shot three Israeli hostages as they waved a white flag.
        And even when Israeli forces have made every possible effort to
        distinguish between combatants and civilians, targeting the one without
        killing the other has proved nearly impossible. Given Gaza’s
        extraordinary population density, almost any military target is in,
        near, above, or below buildings in which large numbers of civilians live
        or work.
      </p>
      <p>
        以色列在加沙的空袭和地面行动背后的军事逻辑，在一定程度上是数十年来美国和以色列共同促成的逐步变化的结果。哈马斯既是非国家行为体，又是加沙事实上的治理者。要区分谁是哈马斯战士、谁不是，特别是在空中，是很困难的。即便是在地面上，以色列部队也常常无法区分平民和战斗员，就像2023年12月，以色列部队向举着白旗的三名以色列人质开枪一样。即使以色列部队竭尽全力区分战斗员和平民，也不大可能在不伤害后者的情况下打击前者。鉴于加沙极为稠密的人口，几乎任何军事目标都位于、邻近、高于或低于大量平民居住或工作的建筑之中。
      </p>
    </div>
    <div>
      <p>
        In Gaza, there are few objects or structures that Israel does not
        consider dual use. Israel has worsened Gaza’s humanitarian crisis by
        holding at the border items such as oxygen cylinders and tent poles.
        Meanwhile, it treats hospitals, schools, apartment buildings, and even
        places of worship as legitimate military targets if Hamas has used them
        for military purposes. Israel maintains that Hamas knows the law of war
        and has sought to protect its military infrastructure by hiding its
        activities in tunnels under civilian structures, such as hospitals, that
        the law protects from attack. Israel emphasized this point in its
        defense before the International Court of Justice against South Africa’s
        claims that Israel is committing genocide in Gaza.
      </p>
      <p>
        在加沙，几乎没有以色列认为不具有双重用途的物体或结构。以色列在边境扣留氧气瓶和帐篷杆等物品，加剧了加沙的人道主义危机。与此同时，如果哈马斯将医院、学校、公寓楼甚至宗教场所用于军事目的，以色列将其视为合法的军事目标。以色列坚持认为，哈马斯了解战争法，并试图通过将其活动隐藏在民用建筑(如医院)下面的隧道中来保护其军事基础设施，而法律保护这些建筑不受攻击。以色列在国际法院针对南非关于以色列在加沙犯下种族灭绝的指控进行辩护时强调了这一点。
      </p>
    </div>
    <div>
      <p>
        Israel’s decision to treat locations traditionally protected from attack
        as legitimate targets has meant devastation for civilians in Gaza.
        Hospitals and schools where those displaced by the war sought refuge
        have been targeted in large-scale attacks, killing thousands. The
        problem has been compounded by Israel’s expansive interpretation of
        proportionality. As Eylon Levy, an Israeli government spokesperson, told
        the BBC, proportionality in Israel’s view means that the collateral
        damage of a given strike must be proportionate to the expected military
        advantage. “And the expected military advantage here,” he explained, “is
        to destroy the terror organization that perpetrated the deadliest
        massacre of Jews since the Holocaust.”
      </p>
      <p>
        以色列对传统上免于攻击的地点采取了合法打击的决定，这意味着加沙的平民遭到了毁灭性的打击。在战争中流离失所的人寻求庇护的医院和学校遭到大规模袭击，导致数千人丧生。而以色列对相称原则的宽泛解释则使问题雪上加霜。以色列政府发言人伊利昂·列维(Erron
        Levy)对BBC表示，在以色列看来，相称性意味着针对特定目标可能造成的附带损害，必须与预期的军事优势相称。“而这里的预期军事优势，”他解释说，“就是摧毁自大屠杀以来对犹太人犯下最严重屠杀罪行的恐怖组织。”
      </p>
    </div>
    <div>
      <p>
        Israel has turned a principle that was meant to shield civilians into a
        tool to justify violence. Its approach to assessing proportionality—not
        strike by strike but in light of the entire war aim—is not how
        militaries are supposed to carry out their assessments. Rather,
        according to international law as codified in Additional Protocol I, the
        principle of proportionality prohibits a given attack where the expected
        harm to civilian people and places is “excessive” compared with the
        “direct military advantage” that the attack is supposed to achieve. By
        weighing any single instance of harm to civilians against a perceived
        existential threat, Israel can justify virtually any strike as meeting
        the requirements of proportionality; the purported benefits always
        outweigh any costs. Unsurprisingly, this approach has led to a war with
        few restraints.
      </p>
      <p>
        以色列把一项旨在保护平民的法则变成了一种为其暴力行为正名的工具。它对衡量比例性的方法——不是针对每一次打击，而是根据整个战争目标来衡量——并不符合军队应有的评估方式。根据《第一附加议定书》中编纂的国际法，比例性原则禁止对预计会带来“过分伤害”给平民和地点的攻击，即便这种攻击被认为能够带来“直接的军事利益”。通过将任何一次对平民的伤害与认为存在的生存威胁相权衡，以色列可以为几乎每一次打击辩解为符合比例性要求；预期的利益总能超过任何代价。不出所料，这种做法导致了一场毫无顾忌的战争。
      </p>
    </div>
    <div>
      <h3>CAUGHT IN THE CROSSFIRE</h3>
      <h3>战火纷飞</h3>
    </div>
    <div>
      <p>
        Although civilians have been killed at extraordinary rates in the war in
        Gaza, they have also suffered extensively in other recent conflicts.
        During the Syrian civil war, the Syrian government repeatedly gassed its
        own people, wiping out entire neighborhoods in an effort to suppress the
        opposition. In 2018, a UN report found that Syrian forces, supported by
        the Russian military, had attacked hospitals, schools, and markets.
      </p>
      <p>
        尽管加沙战争中的平民死亡人数高得惊人，但最近其他冲突中的平民也遭受了巨大损失。在叙利亚内战期间，叙政府多次对平民使用化学武器，夷平整个社区以镇压反抗者。2018年，联合国一份报告发现，叙政府军在俄罗斯军队的支持下，袭击了医院、学校和市场。
      </p>
    </div>
    <div>
      <p>
        Saudi Arabia, too, has been accused of violating legal protections for
        civilians in its operations against Iranian-backed Houthi rebels in
        Yemen. In 2015, Saudi Arabia led a coalition of states in a campaign to
        defeat the Houthis, who had launched cross-border attacks against it and
        seized the Yemeni capital, Sanaa. A team of UN investigators found that
        coalition airstrikes—which the United States supported with midair
        refueling, intelligence, and arms sales—had hit residential areas,
        markets, funerals, weddings, detention facilities, civilian boats, and
        medical facilities, killing more than 6,000 civilians and wounding over
        10,000. The strikes on essential infrastructure, including water
        treatment plants, created a cholera epidemic that killed thousands, most
        of them children.
      </p>
      <p>
        沙特在针对伊朗支持的胡塞叛军的行动中，也涉嫌违反了保护平民的法律。2015年，沙特领导了一个国家联盟，对胡塞发动攻势，因为胡塞向沙特境内发射了跨境攻击，并占领了也门首都萨那。一支联合国调查小组发现，美国通过空中加油、情报和军火销售支持的沙特联军空袭，轰炸了居民区、市场、葬礼、婚礼、拘留设施、民用船只和医疗机构，造成6000多名平民死亡、超过1万人受伤。对包括水处理厂在内的关键基础设施的轰炸，制造了一场导致数千人死亡的霍乱疫情，其中大多数是儿童。
      </p>
    </div>
    <div>
      <p>
        Ukraine has also been the site of barbaric attacks against civilians.
        Russian forces carried out summary executions, disappearances, and
        torture in Bucha and beyond. They indiscriminately bombed Mariupol,
        damaging 77 percent of the city’s medical facilities in the process.
        Throughout the war, Russia’s attacks on Ukraine’s energy grid have left
        millions of civilians without electricity, water, or heat.
      </p>
      <p>
        平民也遭到野蛮袭击。俄罗斯军队在布查及其他地区实施了即决处决、绑架和酷刑。他们对马里乌波尔进行了无差别轰炸，导致该市77%的医疗机构受损。整个战争期间，俄罗斯对乌克兰能源基础设施的袭击已让数百万平民断电、断水、断暖。
      </p>
    </div>
    <div>
      <p>
        Meanwhile, technological innovations threaten to further erode the line
        between civilians and combatants. In Ukraine, for example, the same app
        that Ukrainians use to file taxes can also be used to track Russian
        troops. Using an “e-Enemy” feature, Ukrainians can submit reports,
        photos, and videos of Russian troop movements. Yet this makes those same
        civilians vulnerable to attack, since any civilian who uses the app to
        alert Ukrainian forces of Russian military activity might be regarded as
        “directly participating in hostilities” and therefore considered a
        legitimate target. Ukrainian data servers store both military and
        civilian information, likely rendering computer networks and the
        information stored in them dual-use objects. Ukraine created an “IT
        army” of more than 400,000 volunteers who work with Ukraine’s Defense
        Ministry to launch cyberattacks on Russian infrastructure. These
        Ukrainians may not realize that by volunteering their services, they
        have, according to international law, become combatants in an armed
        conflict.
      </p>
      <p>
        与此同时，技术进步还可能进一步模糊平民与战斗人员之间的界限。例如，在乌克兰，国民用来缴税的应用程序也可以用来追踪俄罗斯军队的行踪。通过“电子敌人”功能，乌克兰人可以提交有关俄罗斯军队调动的报告、照片和视频。但这让这些平民变得更容易受到攻击，因为任何使用该应用向乌军通报俄罗斯军事活动的平民可能会被视为“直接参与敌对行动”，从而成为合法打击目标。乌克兰的数据库存储了军事和民用信息，这很可能使计算机网络及其所存储的信息成为具有双重用途的对象。乌克兰建立了一支由40多万名志愿者组成的“IT大军”，与乌国防部合作对俄罗斯基础设施发起网络攻击。这些乌克兰人可能没有意识到，根据国际法，他们通过提供这些服务已成为武装冲突中的战斗员。
      </p>
    </div>
    <div>
      <h3>CAUSE FOR CONSTRAINT</h3>
      <h3>限制的理由</h3>
    </div>
    <div>
      <p>
        One pessimistic takeaway from the wars in Gaza and Ukraine may be that
        the hard-won lessons of World War II have been forgotten and efforts to
        use law to protect civilians from war are pointless. But as brutal as
        the current conflicts are, they would likely be even more horrific
        without these rules. A careful reading of the current era would show
        that rather than altogether abandoning the protections of civilians
        enshrined in the Geneva Conventions, belligerents in recent wars have
        been making those protections less effective by severely restricting
        what counts as civilian. And the United States has played a key part in
        this shift.
      </p>
      <p>
        从加沙和乌克兰的战事中，我们可以悲观地得出结论，二战时艰难赢得的教训已经被遗忘，利用法律保护平民免受战争之苦的努力注定徒劳无功。但尽管当前冲突如此惨烈，如果没有这些规则，局面可能更加骇人。仔细审视当今时代会发现，交战国非但没有完全抛弃《日内瓦公约》对平民的保护，反而通过严格限制何为平民，使这些保护变得无效。而美国在这一转变中起了关键作用。
      </p>
    </div>
    <div>
      <p>
        Since 9/11, Washington has used its power to weaken constraints on the
        use of force, aggressively interpret the right to self-defense, and
        allow for more expansive targeting of dual-use sites and structures.
        These positions have created greater flexibility for the U.S. military,
        but they have also placed more civilians in harm’s way. Following the
        United States’ lead, other countries, including France, Israel, Saudi
        Arabia, Turkey, and the United Kingdom, have likewise loosened
        constraints on their own militaries.
      </p>
      <p>
        自9·11以来，华盛顿利用其影响力削弱了使用武力的限制，对自卫权做出了富有进攻性的解读，并允许更广泛地针对具有双重用途的地点和设施。这些立场为美军提供了更大的灵活性，但也使更多平民处于危险之中。在美国的带头作用下，包括法国、以色列、沙特、土耳其和英国在内的其他国家，也都放松了对其军队的限制。
      </p>
    </div>
    <div>
      <p>
        To reverse this trend and strengthen the law of armed conflict,
        Washington must decide that embracing constraints and pressing others to
        do the same is essential to the fundamental principles of human dignity
        that the United States, at its best, has championed. To its credit, the
        Biden administration has already taken some modest steps in this
        direction. In 2022, the Defense Department announced a detailed plan for
        how the U.S. military would better protect civilians, and this February,
        the Biden administration said that it would require foreign governments
        to promise that any U.S. weapons they received would not be used to
        violate international law. But much more remains to be done.
      </p>
      <p>
        要扭转这一趋势并强化武装冲突法，美国必须认定，接受约束并促使其他国家也这样做对于美国在最好的时候所捍卫的人的尊严这一基本原则至关重要。拜登政府在这方面采取了一些有限的步骤，值得称赞。2022年，五角大楼公布了美国军方如何更好地保护平民的详细计划，今年2月，拜登政府表示，将要求外国政府承诺不会使用从美国获得的任何武器来违反国际法。但还有很多工作要做。
      </p>
    </div>
    <div>
      <p>
        For starters, the United States should expand collaboration and
        cooperation with the International Criminal Court, the most effective
        international mechanism for enforcing international humanitarian law.
        Indeed, members of the U.S. Congress have cheered the ICC’s exercise of
        jurisdiction over Russia for crimes committed during the war in Ukraine
        and passed a law allowing the United States to share evidence of Russian
        war crimes in Ukraine with its prosecutor. Yet in 2020, the Trump
        administration sanctioned ICC judges and lawyers in retaliation for
        having investigated whether U.S. soldiers committed war crimes in
        Afghanistan. To the rest of the world, the hypocrisy is glaring and
        instructive. One way for the United States to improve its relationship
        with the court would be to repeal the American Service-Members’
        Protection Act, a 2002 law, known colloquially as “the Hague Invasion
        Act,” that allows the president to order military action to protect
        Americans from ICC prosecution. It also prohibits government agencies
        from assisting the court unless specifically permitted, as with the
        Ukraine investigation.
      </p>
      <p>
        首先，美国应加强与国际刑事法院——执行国际人道法的最有效的国际机制——的合作。事实上，美国国会议员曾为国际刑事法院对俄罗斯在乌克兰战争中所犯罪行行使管辖权欢呼，并通过了一项法律，允许美国与该法院检察官分享关于俄罗斯在乌克兰犯下的战争罪的证据。但2020年，特朗普政府以调查美军在阿富汗可能犯下战争罪为由，制裁了国际刑事法院的法官和律师。这在全世界看来是极其虚伪的，也是具有教育意义的。美国改善与该法院关系的一种方式是废除《美国军人保护法》(American
        Service-Members’ Protection
        Act)。该法于2002年出台，俗称“海牙入侵法”，授权总统采取军事行动保护美国人免受国际刑事法院的起诉。它还禁止联邦机构协助该法院，除非得到明确许可——比如在乌克兰问题上的调查。
      </p>
    </div>
    <div>
      <p>
        The United States should also reconsider some of the expansive legal
        positions it adopted after 9/11. It should, for example, endorse more
        stringent limits on when dual-use objects can be targeted. It should
        revise the treatment of the principles of proportionality and feasible
        precautions in the Defense Department’s Law of War Manual to better
        reflect international humanitarian law. And it should fully implement
        its new plan to mitigate civilian harm during U.S. military operations.
      </p>
      <p>
        美国也应该重新考虑它在9·11事件后采取的一些过度法律措施。例如，它应该支持对可以攻击的双重用途物体施加更严格的限制。它应该修改国防部《战争法手册》中关于比例原则和可行预防措施的内容，以更好地反映国际人道主义法。此外，它还应全面实施新的计划，在美军行动中减轻对平民的伤害。
      </p>
    </div>
    <div>
      <p>
        The United States should also restrict its military assistance to those
        countries that comply with international humanitarian law—not just when
        providing arms but also when offering financial support, intelligence,
        and training. The United States has counterterrorism programs in some 80
        countries on six continents. If Washington conditioned its support on
        greater adherence to the law—and withdrew it from countries that didn’t
        comply—the effect would be powerful and immediate. And Israel should not
        be exempt from those standards; the United States should insist that the
        country make clear the concrete steps it intends to take to ensure that
        its conduct of the war in Gaza comports with international law.
      </p>
      <p>
        美国也应当限制其对遵守国际人道主义法的国家的军事援助——不仅是在提供武器时，而且在提供财政支持、情报和培训时也是如此。美国在六大洲80多个国家中开展反恐项目。如果华盛顿将援助条件与对法律的遵守挂钩，并对不合规的国家撤回援助，其影响立竿见影。以色列不应被免于此标准；美国应坚持要求以色列明确说明打算采取哪些具体步骤，以确保其在加沙的战争行为符合国际法。
      </p>
    </div>
    <div>
      <p>
        These changes should be made not only as a matter of policy but also as
        a matter of law. When the executive branch offers legal explanations for
        U.S. behavior, it almost always does so to justify taking military
        action, often in ways that push existing legal boundaries. By contrast,
        when it endorses restraints that better protect civilians in war, it has
        generally emphasized that it is doing so only as a matter of policy—not
        because it is required but as a choice. This means the restraints can be
        easily discarded when they become inconvenient. The legal rationales for
        acting, meanwhile, stand as precedents to justify the United States’
        future military operations—and those of other countries around the
        world.
      </p>
      <p>
        这些改变不仅应当作为政策问题加以推进，还应当在法律层面加以巩固。美国行政部门为其行为提供法律解释时，几乎总是为了证明采取军事行动的合理性，而且往往是将法律限制推至极限。相比之下，当它支持采取更好的措施以保护战争中的平民时，通常会强调这样做只是为了政策考虑，而非迫于必要，而是出于选择。这意味着，一旦这些限制变得不便，就可以轻易抛弃。与此同时，采取行动的法律理由为美国以及世界各地其他国家未来军事行动提供了先例。
      </p>
    </div>
    <div>
      <p>
        If the law of war is to survive today’s existential challenges, the
        United States and its allies need to treat it not as an optional
        constraint to be adjusted or shrugged off as needed but as an unmoving
        pillar of the global legal order. True, there will be wartime actors who
        break the law, and civilians will continue to suffer as a result. But
        before the United States can hold these offenders to account, it must
        show that it is prepared to hold its own forces—and those of its
        allies—to the same standards.
      </p>
      <p>
        要想让战争法能够经受住当前的生存挑战，美国及其盟友就不能将其视为可有可无的限制，想怎么调整就怎么调整，或者干脆无视。当然，会有交战方违法，因此平民将继续遭受苦难。但在此之前，美国必须表明，它准备让本国及盟友部队也遵守同样的标准。
      </p>
    </div>
