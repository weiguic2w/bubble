      <h2>Gaza, Ukraine, and the Breakdown of International Law</h2>
      <h2>加沙、乌克兰与国际法的破产</h2>
    </div>
    <div>
      <p>
        Hamas’s attack on Israel and Israel’s response to it have been a
        disaster for civilians. In its October 7 massacre, Hamas sought out
        unarmed Israeli civilians, including women, children, and the elderly,
        killing close to 1,200 people and taking around 240 hostages. Israel’s
        subsequent air and ground campaign in Gaza has, as of March 2024, killed
        more than 30,000 people, an estimated two-thirds of whom were women and
        children. The Israeli offensive has also displaced some two million
        people (more than 85 percent of the population of Gaza), left more than
        a million people at risk of starvation, and damaged or destroyed some
